"""
剧本操作 — LLM可调用的剧本管理操作
开始录制/停止录制/回放剧本/列出剧本/删除剧本
"""
import json
from .基类 import 操作结果, 操作基类

# 全局剧本管理器引用
_剧本管理器 = None


def 设置剧本管理器(实例):
    global _剧本管理器
    _剧本管理器 = 实例


class 开始录制(操作基类):
    名称 = "开始录制"
    描述 = "开始录制操作步骤为剧本（后续所有操作将被记录）"
    参数结构 = {
        "名称": {"类型": "字符串", "必填": False, "说明": "剧本名称（留空自动生成）"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _剧本管理器:
            return 操作结果.失败("剧本管理器未初始化")
        名称 = 参数.get("名称", "")
        结果 = _剧本管理器.开始录制(名称)
        if 结果.get("成功"):
            return 操作结果.成功(f"已开始录制剧本 [{结果['名称']}]", 元数据=结果)
        return 操作结果.失败(结果.get("错误", "录制启动失败"))


class 停止录制(操作基类):
    名称 = "停止录制"
    描述 = "停止录制并保存剧本"
    参数结构 = {}

    def 执行(self, 参数: dict) -> 操作结果:
        if not _剧本管理器:
            return 操作结果.失败("剧本管理器未初始化")
        结果 = _剧本管理器.停止录制()
        if 结果.get("成功"):
            剧本 = 结果.get("剧本", {})
            return 操作结果.成功(
                f"已保存剧本 [{剧本.get('名称', '')}]，共 {结果['步数']} 步",
                元数据=结果
            )
        return 操作结果.失败(结果.get("错误", "没有正在录制的剧本"))


class 回放剧本(操作基类):
    名称 = "回放剧本"
    描述 = "按名称加载并逐步回放剧本（支持变量传递）"
    参数结构 = {
        "名称": {"类型": "字符串", "必填": True, "说明": "剧本名称"},
        "变量": {"类型": "字符串", "必填": False, "说明": "JSON格式的变量字典（可选）"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _剧本管理器:
            return 操作结果.失败("剧本管理器未初始化")
        名称 = 参数.get("名称", "")
        变量文本 = 参数.get("变量", "")
        变量 = None
        if 变量文本:
            try:
                变量 = json.loads(变量文本)
            except json.JSONDecodeError:
                return 操作结果.失败("变量格式错误，需为合法JSON")
        结果 = _剧本管理器.回放by名称(名称, 变量)
        if 结果.get("成功"):
            执行结果 = 结果.get("执行结果", [])
            成功数 = sum(1 for r in 执行结果 if r.get("成功"))
            return 操作结果.成功(
                f"剧本 [{名称}] 回放完成，共 {结果['总步数']} 步，全部成功",
                元数据=结果
            )
        已完成 = 结果.get("已完成步骤", 0)
        if 已完成 > 0:
            return 操作结果.失败(
                f"剧本回放在第{已完成 + 1}步失败: {结果.get('错误', '')}"
            )
        return 操作结果.失败(结果.get("错误", "回放失败"))


class 列出剧本(操作基类):
    名称 = "列出剧本"
    描述 = "查看所有已保存的剧本"
    参数结构 = {}

    def 执行(self, 参数: dict) -> 操作结果:
        if not _剧本管理器:
            return 操作结果.失败("剧本管理器未初始化")
        剧本列表 = _剧本管理器.列出剧本()
        if not 剧本列表:
            return 操作结果.成功("还没有保存任何剧本")
        格式化 = [f"  {b['名称']} (创建: {b.get('创建时间', 'N/A')})" for b in 剧本列表]
        return 操作结果.成功(f"共 {len(剧本列表)} 个剧本:\n" + "\n".join(格式化))


class 删除剧本(操作基类):
    名称 = "删除剧本"
    描述 = "删除指定剧本"
    参数结构 = {
        "名称": {"类型": "字符串", "必填": True, "说明": "要删除的剧本名称"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _剧本管理器:
            return 操作结果.失败("剧本管理器未初始化")
        名称 = 参数.get("名称", "")
        if not 名称:
            return 操作结果.失败("剧本名称为空")
        结果 = _剧本管理器.删除剧本(名称)
        if 结果.get("成功"):
            return 操作结果.成功(f"已删除剧本 [{名称}]")
        return 操作结果.失败("删除失败")
