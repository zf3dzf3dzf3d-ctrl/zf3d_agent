"""任务账本操作 — 让AI动态管理任务账本（对标微软Magentic模式）

AI可以查看账本状态、添加新任务、标记任务完成。
"""
import json
from .基类 import 操作结果, 操作基类


# 全局账本实例引用
_账本实例 = None


def 设置账本实例(实例):
    global _账本实例
    _账本实例 = 实例


class 查看任务账本(操作基类):
    名称 = "查看任务账本"
    描述 = "查看当前任务账本的状态和进度。显示所有任务的完成情况和结果。"
    参数结构 = {}

    def 执行(self, 参数: dict) -> 操作结果:
        if not _账本实例:
            return 操作结果.失败("任务账本未初始化（当前没有执行中的计划）")
        摘要 = _账本实例.获取状态摘要()
        if not 摘要:
            return 操作结果.成功("当前没有活跃的任务账本。")
        return 操作结果.成功(摘要)


class 添加任务(操作基类):
    名称 = "添加任务"
    描述 = "向任务账本添加新发现的子任务。当执行中发现需要额外步骤时使用。"
    参数结构 = {
        "说明": {"类型": "字符串", "必填": True, "说明": "任务说明"},
        "预计操作": {"类型": "字符串", "必填": False, "说明": "预计使用的操作名称"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _账本实例:
            return 操作结果.失败("任务账本未初始化")
        说明 = 参数.get("说明", "").strip()
        预计操作 = 参数.get("预计操作", "").strip()
        if not 说明:
            return 操作结果.失败("任务说明为空")
        task_id = _账本实例.添加任务(说明, 预计操作)
        if task_id < 0:
            return 操作结果.失败("任务数量已达上限")
        return 操作结果.成功(f"✅ 已添加任务 [{task_id}]: {说明}")


class 完成任务(操作基类):
    名称 = "完成任务"
    描述 = "标记任务账本中的任务为完成状态。"
    参数结构 = {
        "任务ID": {"类型": "整数", "必填": True, "说明": "要完成的任务ID"},
        "结果": {"类型": "字符串", "必填": False, "说明": "任务执行结果摘要"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _账本实例:
            return 操作结果.失败("任务账本未初始化")
        task_id = 参数.get("任务ID", 0)
        结果 = 参数.get("结果", "")
        if _账本实例.完成任务(task_id, 结果):
            return 操作结果.成功(f"✅ 任务 [{task_id}] 已标记完成")
        return 操作结果.失败(f"任务 [{task_id}] 不存在")
