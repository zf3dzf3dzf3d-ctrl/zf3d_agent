"""
股票预测操作 - ZF3D Agent集成
调用大模型项目的Agent接口, 支持单股预测/批量预测/智能选股
本地直调(有模型) / HTTP远程(无模型, 需授权)
"""
import json
import os
import sys

from 操作.基类 import 操作结果, 操作基类


class 股票预测(操作基类):
    """股票预测 - AI大模型预测涨跌和选股"""
    名称 = "股票预测"
    描述 = "AI股票预测大模型: 单股预测涨跌概率、批量预测、多周期智能选股(动态加权投票, 已排除涨停跌停)"
    参数结构 = {
        "模式": {"类型": "字符串", "必填": False, "说明": "单股(默认)/批量/选股"},
        "股票代码": {"类型": "字符串", "必填": False, "说明": "股票代码，如600519。单股/批量模式必填"},
        "天数": {"类型": "整数", "必填": False, "说明": "预测未来几天，默认1"},
        "数量": {"类型": "整数", "必填": False, "说明": "选股模式返回Top N，默认5"},
    }

    def 执行(self, 参数: dict) -> 操作结果:
        模式 = 参数.get("模式", "单股")
        股票代码 = 参数.get("股票代码", "")
        天数 = 参数.get("天数", 1)
        数量 = 参数.get("数量", 5)

        try:
            # 大模型项目路径
            股票项目根 = r"F:\ai测试\股票大模型001"
            if not os.path.exists(股票项目根):
                return 操作结果.失败(
                    "AI股票大模型项目未安装\n"
                    "如需使用AI选股/预测功能, 请到朱峰社区购买授权\n"
                    "官网: https://www.zf3d.com"
                )

            if 股票项目根 not in sys.path:
                sys.path.insert(0, 股票项目根)

            from Agent接口 import 处理请求

            调用参数 = {"模式": 模式, "预测天数": 天数, "数量": 数量}
            if 股票代码:
                调用参数["股票代码"] = 股票代码

            结果json = 处理请求(调用参数)
            结果 = json.loads(结果json)

            if "错误" in 结果:
                return 操作结果.失败(结果["错误"])

            # 选股模式
            if 模式 == "选股":
                选股列表 = 结果.get("选股结果", [])
                if not 选股列表:
                    return 操作结果.失败("选股结果为空, 可能模型未训练")

                文本 = "AI智能选股Top " + str(len(选股列表)) + "\n"
                文本 += "=" * 70 + "\n"
                文本 += f"{'#':>3} {'代码':<8} {'名称':<10} {'涨%':>6} {'投票':>6} {'置信':>6} {'收盘':>8} {'状态':>4}\n"
                文本 += "-" * 70 + "\n"
                for i, r in enumerate(选股列表, 1):
                    文本 += f"{i:>3} {r.get('代码',''):<8} {r.get('名称',''):<10} {r.get('涨概率',0):>5.1f}% {r.get('投票',''):>6} {r.get('置信度',0):>+5.1f}% {r.get('收盘',0):>8.2f} {r.get('状态',''):>4}\n"
                文本 += "=" * 70 + "\n"
                文本 += "投票=看涨周期数/总周期数(动态加权)\n"

                return 操作结果.成功(数据=文本, 元数据={
                    "操作类型": "AI选股",
                    "选股结果": json.dumps(选股列表, ensure_ascii=False)
                })

            # 单股/批量预测
            涨跌概率 = 结果.get("涨跌概率", {})
            上涨 = 涨跌概率.get("涨", 0) if isinstance(涨跌概率, dict) else 0
            下跌 = 涨跌概率.get("跌", 0) if isinstance(涨跌概率, dict) else 0
            持平 = 涨跌概率.get("平", 0) if isinstance(涨跌概率, dict) else 0
            名称 = 结果.get("股票名称", 股票代码)
            置信度 = 结果.get("置信度", 0)

            if 上涨 >= 下跌 and 上涨 >= 持平:
                结论 = "看涨"
            elif 下跌 >= 上涨 and 下跌 >= 持平:
                结论 = "看跌"
            else:
                结论 = "横盘震荡"

            文本 = f"""AI股票预测报告
{'='*40}
股票: {名称} ({股票代码})
预测: 未来{天数}天

上涨概率: {上涨*100:.1f}%
下跌概率: {下跌*100:.1f}%
持平概率: {持平*100:.1f}%

结论: {结论}
置信度: {置信度*100:.1f}%"""

            return 操作结果.成功(数据=文本, 元数据={
                "操作类型": "股票预测",
                "股票代码": 股票代码,
                "预测结果": 结果json
            })

        except Exception as e:
            return 操作结果.失败(
                f"AI预测服务异常: {e}\n"
                "如需使用AI选股/预测功能, 请到朱峰社区购买授权\n"
                "官网: https://www.zhufengshequ.com"
            )
