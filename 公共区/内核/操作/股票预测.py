"""
股票预测操作 - 集成到ZF3D Agent
通过加载训练好的LSTM模型权重，预测股票涨跌
"""
import json
import os
import sys

from 操作.基类 import 操作结果, 操作基类


class 股票预测(操作基类):
    """股票预测操作 - 加载训练好的模型权重，预测指定股票涨跌"""
    名称 = "股票预测"
    描述 = "使用训练好的LSTM模型预测股票涨跌概率。加载模型权重和缓存数据，返回涨/跌/平三分类概率。支持指定股票代码。"
    参数结构 = {
        "股票代码": {"类型": "字符串", "必填": False, "说明": "股票代码，如600519(贵州茅台)。不填则使用默认股票"},
        "天数": {"类型": "整数", "必填": False, "说明": "预测未来几天，默认1天"},
    }

    def 执行(self, 参数: dict) -> 操作结果:
        股票代码 = 参数.get("股票代码", "600519")
        天数 = 参数.get("天数", 1)

        try:
            # 项目根目录
            项目根 = os.path.dirname(os.path.abspath(__file__))
            # 股票模型项目根
            股票项目根 = os.path.abspath(os.path.join(项目根, "..", "..", "..", "F:/ai测试/股票大模型001"))

            if not os.path.exists(股票项目根):
                # 尝试相对路径
                股票项目根 = os.path.abspath(os.path.join(项目根, "..", "..", "..", "股票大模型001"))

            if not os.path.exists(股票项目根):
                return 操作结果.失败(f"股票大模型项目目录不存在，请确认路径")

            # 添加股票项目到系统路径
            if 股票项目根 not in sys.path:
                sys.path.insert(0, 股票项目根)

            # 导入预测引擎
            from 预测引擎 import 预测引擎

            # 查找权重文件
            模型输出目录 = os.path.join(股票项目根, "模型输出")
            权重文件 = None

            # 查找最佳权重
            候选路径 = [
                os.path.join(模型输出目录, "最佳_权重.json"),
                os.path.join(模型输出目录, "模型权重", "最佳_权重.json"),
                os.path.join(模型输出目录, f"{股票代码}_权重.json"),
            ]
            for 路径 in 候选路径:
                if os.path.exists(路径):
                    权重文件 = 路径
                    break

            if not 权重文件:
                return 操作结果.失败(f"未找到训练好的模型权重文件，请先训练模型")

            # 创建预测引擎并预测
            引擎 = 预测引擎(模型路径=权重文件)
            结果 = 引擎.预测单股(股票代码, 预测天数=天数)

            if "错误" in 结果:
                return 操作结果.失败(f"预测失败: {结果['错误']}")

            # 提取关键信息
            涨跌概率 = 结果.get("涨跌概率", {})
            上涨概率 = 涨跌概率.get("涨", 0) if isinstance(涨跌概率, dict) else 0
            下跌概率 = 涨跌概率.get("跌", 0) if isinstance(涨跌概率, dict) else 0
            持平概率 = 涨跌概率.get("平", 0) if isinstance(涨跌概率, dict) else 0
            置信度 = 结果.get("置信度", 0)
            股票名称 = 结果.get("股票名称", 股票代码)

            # 确定结论
            if 上涨概率 >= 下跌概率 and 上涨概率 >= 持平概率:
                结论 = "看涨 📈"
            elif 下跌概率 >= 上涨概率 and 下跌概率 >= 持平概率:
                结论 = "看跌 📉"
            else:
                结论 = "横盘震荡 ➡️"

            文本输出 = f"""📊 股票预测报告
━━━━━━━━━━━━━━━━━━
股票: {股票名称} ({股票代码})
预测: 未来{天数}天

📈 上涨概率: {上涨概率*100:.1f}%
📉 下跌概率: {下跌概率*100:.1f}%
➡️ 持平概率: {持平概率*100:.1f}%

结论: {结论}
置信度: {置信度*100:.1f}%

参考数据: {结果.get('参考数据', {}).get('数据范围', '未知')}
模型: {结果.get('模型信息', {}).get('类型', 'LSTM')}
"""
            return 操作结果.成功(数据=文本输出, 元数据={
                "操作类型": "股票预测",
                "股票代码": 股票代码,
                "股票名称": 股票名称,
                "预测结果": json.dumps(结果, ensure_ascii=False)
            })

        except ImportError as e:
            return 操作结果.失败(f"导入预测引擎失败: {e}，请确保股票大模型项目完整")
        except Exception as e:
            return 操作结果.失败(f"股票预测失败: {e}")
