"""
图片处理操作 - 基于OpenCV的图像处理功能
去水印/去杂物/亮度对比度/裁剪/缩放/模糊/灰度化/旋转
依赖: opencv-python (pip install opencv-python)
"""
import os
from pathlib import Path
from .基类 import 操作结果, 操作基类


def _检查opencv():
    """检查opencv是否已安装"""
    try:
        import cv2
        return True, cv2
    except ImportError:
        return False, None


def _解析路径(路径, 工作目录=None):
    """解析相对路径为绝对路径"""
    p = Path(路径)
    if not p.is_absolute():
        if 工作目录:
            p = Path(工作目录) / p
        else:
            p = Path.cwd() / p
    return p


def _生成输出路径(输入路径, 保存路径=None, 后缀="_edited"):
    """生成输出路径"""
    if 保存路径:
        return Path(保存路径)
    输入 = Path(输入路径)
    return 输入.parent / f"{输入.stem}{后缀}{输入.suffix}"


