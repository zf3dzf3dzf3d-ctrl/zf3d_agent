    名称 = "图片去水印"
    描述 = "去除图片上的水印、文字、logo、污渍、划痕等。需要提供遮罩图（黑白图，白色区域=要去除的部分）。算法: TELEA(快速行进,默认) 或 NS(流体力学,更精细)"
    参数结构 = {
        "图片路径": {"类型": "字符串", "必填": True, "说明": "要处理的图片路径"},
        "遮罩路径": {"类型": "字符串", "必填": True, "说明": "黑白遮罩图路径，白色(255)=要去除的区域，黑色(0)=保留的区域"},
        "修复半径": {"类型": "整数", "必填": False, "说明": "修复区域的半径(像素)，默认3，值越大修复范围越大但可能模糊"},
        "算法": {"类型": "字符串", "必填": False, "说明": "修复算法: TELEA(快速行进,默认) 或 NS(流体力学,更精细)"},
        "保存路径": {"类型": "字符串", "必填": False, "说明": "结果保存路径，不填则在原图同目录生成_去水印后文件"},
    }

    def 执行(self, 参数: dict) -> 操作结果:
        已安装, cv2 = _检查opencv()
        if not 已安装:
            return 操作结果.失败("opencv-python未安装，请运行: pip install opencv-python")

        图片路径 = 参数.get("图片路径", "").strip()
        遮罩路径 = 参数.get("遮罩路径", "").strip()
        修复半径 = int(参数.get("修复半径", 3))
        算法 = 参数.get("算法", "TELEA").strip().upper()
        保存路径 = 参数.get("保存路径", "").strip()

        if not 图片路径:
            return 操作结果.失败("图片路径为空")
        if not 遮罩路径:
            return 操作结果.失败("遮罩路径为空")

        图片路径 = str(_解析路径(图片路径, getattr(self, "当前工作目录", None)))
        遮罩路径 = str(_解析路径(遮罩路径, getattr(self, "当前工作目录", None)))
        输出路径 = _生成输出路径(图片路径, 保存路径 if 保存路径 else None, "_去水印")

