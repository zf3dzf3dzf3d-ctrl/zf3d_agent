class 读取页面元素操作(操作基类):
    名称 = "读取页面元素"
    描述 = "提取当前网页中特定类型的元素（链接/图片/表单/表格/按钮/输入框）"
    参数结构 = {
        "元素类型": {"类型": "字符串", "必填": True,
                   "说明": "元素类型：链接/图片/表单/表格/按钮/输入框"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        错误 = _确保Playwright已安装()
        if 错误:
            return 操作结果.失败(错误)

        元素类型 = 参数.get("元素类型", "")
        合法类型 = ["链接", "图片", "表单", "表格", "按钮", "输入框"]
        if 元素类型 not in 合法类型:
            return 操作结果.失败(f"元素类型必须是: {'/'.join(合法类型)}")

        try:
            引擎 = _获取浏览器引擎()
            元素列表 = 引擎.提取元素列表(元素类型)

            if not 元素列表:
                return 操作结果.成功(f"页面中未找到{元素类型}元素")

            文本行 = []
            for i, 项 in enumerate(元素列表, 1):
                行 = f"{i}. "
                if "名称" in 项:
                    行 += f'{项["名称"]}'
                if "链接" in 项:
                    行 += f' → {项["链接"]}'
                if "源地址" in 项:
                    行 += f' [img: {项["源地址"]}]'
                if "提示" in 项:
                    行 += f' (提示: {项["提示"]})'
                if "值" in 项:
                    行 += f' [值: {项["值"]}]'
                文本行.append(行)

            return 操作结果.成功(
                "\n".join(文本行),
                元数据={"操作类型": "读取页面元素", "元素类型": 元素类型, "元素数量": len(元素列表)}
            )
        except Exception as e:
            return 操作结果.失败(f"读取页面元素失败: {e}")


