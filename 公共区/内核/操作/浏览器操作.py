class 打开网页操作(操作基类):
    名称 = "打开网页"
    描述 = "在浏览器中打开指定网址，返回页面标题、类型和内容摘要"
    参数结构 = {
        "网址": {"类型": "字符串", "必填": True, "说明": "要打开的URL地址，如 https://www.example.com"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        错误 = _确保Playwright已安装()
        if 错误:
            return 操作结果.失败(错误)

        网址 = 参数.get("网址", "")
        if not 网址:
            return 操作结果.失败("网址为空")

        if not 网址.startswith(("http://", "https://")):
            网址 = "https://" + 网址

