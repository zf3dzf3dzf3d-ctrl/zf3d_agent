class 分析网页操作(操作基类):
    名称 = "分析网页"
    描述 = "深度分析当前网页内容，返回结构化分析结果（页面类型/主要内容/元素统计/列表数据/表格数据）"
    参数结构 = {
        "分析目标": {"类型": "字符串", "必填": True,
                   "说明": "分析目标：摘要/结构/内容/列表/表格/表单"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        错误 = _确保Playwright已安装()
        if 错误:
            return 操作结果.失败(错误)

        目标 = 参数.get("分析目标", "摘要")
        合法目标 = ["摘要", "结构", "内容", "列表", "表格", "表单"]
        if 目标 not in 合法目标:
            return 操作结果.失败(f"分析目标必须是: {'/'.join(合法目标)}")

        try:
            引擎 = _获取浏览器引擎()
            分析器 = _获取页面分析器()
            页面信息 = 引擎.获取页面信息()

            if 目标 == "摘要":
                无障碍树 = 引擎.提取无障碍树()
                页面类型 = 分析器.分析页面类型(无障碍树)
                摘要 = 分析器.生成页面摘要(页面信息, 无障碍树)
                结构文本 = 分析器.格式化无障碍树(无障碍树, 最大深度=3)
                return 操作结果.成功(
                    f"=== 页面摘要 ===\n"
                    f"标题: {页面信息.get('标题', '')}\n"
                    f"网址: {页面信息.get('URL', '')}\n"
                    f"类型: {页面类型}\n"
                    f"摘要: {摘要}\n\n"
                    f"=== 顶层结构 ===\n{结构文本[:2000]}",
                    元数据={"操作类型": "分析网页", "页面类型": 页面类型}
                )

            elif 目标 == "结构":
                无障碍树 = 引擎.提取无障碍树()
                结构文本 = 分析器.格式化无障碍树(无障碍树)
                页面类型 = 分析器.分析页面类型(无障碍树)
                return 操作结果.成功(
                    f"页面类型: {页面类型}\n\n{结构文本}",
                    元数据={"操作类型": "分析网页"}
                )

            elif 目标 == "内容":
                正文 = 引擎.提取正文()
                结构 = 分析器.分析内容结构(正文)
                return 操作结果.成功(
                    f"总字数: {结构.get('总字数', 0)}\n"
                    f"段落数: {结构.get('段落数', 0)}\n"
                    f"有代码: {结构.get('是否有代码', False)}\n"
                    f"有列表: {结构.get('是否有列表', False)}\n\n"
                    f"=== 内容预览 ===\n{结构.get('预览', '')}",
                    元数据={"操作类型": "分析网页", "总字数": 结构.get('总字数', 0)}
                )

            elif 目标 == "列表":
                列表 = 分析器.提取列表数据(引擎)
                if not 列表:
                    return 操作结果.成功("未检测到列表型数据")
                行 = []
                for 项 in 列表:
                    行.append(f"• {项.get('标题', '无标题')}")
                    if 项.get('链接'):
                        行.append(f"  链接: {项['链接']}")
                    if 项.get('作者'):
                        行.append(f"  作者: {项['作者']}")
                    if 项.get('时间'):
                        行.append(f"  时间: {项['时间']}")
                return 操作结果.成功(
                    f"找到{len(列表)}条列表数据:\n\n" + "\n".join(行),
                    元数据={"操作类型": "分析网页", "列表项数": len(列表)}
                )

            elif 目标 == "表格":
                表格 = 分析器.提取表格数据(引擎)
                if not 表格:
                    return 操作结果.成功("未检测到表格数据")
                return 操作结果.成功(
                    json.dumps(表格, ensure_ascii=False, indent=2)[:5000],
                    元数据={"操作类型": "分析网页", "表格行数": len(表格)}
                )

            elif 目标 == "表单":
                元素列表 = 引擎.提取元素列表("输入框")
                按钮列表 = 引擎.提取元素列表("按钮")
                行 = []
                if 元素列表:
                    行.append("输入框:")
                    for i, el in enumerate(元素列表, 1):
                        行.append(f"  {i}. {el.get('名称', '') or el.get('提示', '')} [{el.get('标签', '')}]")
                if 按钮列表:
                    行.append("\n按钮:")
                    for i, el in enumerate(按钮列表, 1):
                        行.append(f"  {i}. {el.get('名称', '')}")
                return 操作结果.成功(
                    "\n".join(行) if 行 else "未检测到表单元素",
                    元数据={"操作类型": "分析网页", "输入框数": len(元素列表), "按钮数": len(按钮列表)}
                )

        except Exception as e:
            return 操作结果.失败(f"分析网页失败: {e}")
