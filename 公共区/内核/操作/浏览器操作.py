class 搜索页面内容操作(操作基类):
    名称 = "搜索页面内容"
    描述 = "在当前网页中搜索指定关键词，返回匹配位置和上下文"
    参数结构 = {
        "关键词": {"类型": "字符串", "必填": True, "说明": "要搜索的文本"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        错误 = _确保Playwright已安装()
        if 错误:
            return 操作结果.失败(错误)

        关键词 = 参数.get("关键词", "")
        if not 关键词:
            return 操作结果.失败("关键词为空")

        try:
            引擎 = _获取浏览器引擎()
            正文 = 引擎.提取正文()
            if not 正文:
                return 操作结果.失败("页面无正文内容")

            # 找所有匹配位置
            结果 = []
            位置 = 0
            while True:
                idx = 正文.find(关键词, 位置)
                if idx == -1:
                    break
                开始 = max(0, idx - 30)
                结束 = min(len(正文), idx + len(关键词) + 30)
                上下文 = 正文[开始:结束].replace("\n", " ")
                结果.append(f"位置{idx}: ...{上下文}...")
                位置 = idx + len(关键词)

            if not 结果:
                return 操作结果.成功(f"未找到关键词「{关键词}」")

            return 操作结果.成功(
                f"找到{len(结果)}处匹配:\n" + "\n".join(结果[:20]),
                元数据={"操作类型": "搜索页面内容", "匹配数": len(结果)}
            )
        except Exception as e:
            return 操作结果.失败(f"搜索页面内容失败: {e}")


