def _加载浏览器配置() -> dict:
    """从JSON文件加载浏览器配置"""
    global _配置缓存
    if _配置缓存 is not None:
        return _配置缓存
    try:
        项目根 = Path(__file__).parent.parent.parent.parent
        配置路径 = 项目根 / "公共区" / "配置" / "浏览器配置.json"
        with open(配置路径, "r", encoding="utf-8") as f:
            _配置缓存 = json.load(f)
    except Exception:
        _配置缓存 = {}
    return _配置缓存


def _获取浏览器引擎():
    """获取全局浏览器引擎实例（惰性初始化）"""
    global _浏览器引擎
    if _浏览器引擎 is None or not _浏览器引擎.已启动:
        # 动态导入避免Playwright未安装时整个模块报错
        import sys
        项目根 = str(Path(__file__).parent.parent.parent.parent)
        if 项目根 not in sys.path:
            sys.path.insert(0, 项目根)
        模块路径 = str(Path(__file__).parent.parent.parent / "模块" / "浏览器")
        if 模块路径 not in sys.path:
            sys.path.insert(0, 模块路径)

        from 浏览器引擎 import 浏览器引擎类
        配置 = _加载浏览器配置()
        _浏览器引擎 = 浏览器引擎类(配置)
        _浏览器引擎.启动()
    return _浏览器引擎


