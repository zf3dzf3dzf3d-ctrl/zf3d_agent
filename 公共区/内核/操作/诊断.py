from .基类 import 操作结果, 操作基类


class 查询运行错误(操作基类):
    名称 = "查询运行错误"
    描述 = "查询系统运行时的错误和警告记录，可用于诊断bug。返回最近的错误列表(含异常类型、来源、堆栈)"
    参数结构 = {
        "未解决Only": {"类型": "布尔", "必填": False, "说明": "是否只返回未解决的错误，默认True"},
        "最近数": {"类型": "整数", "必填": False, "说明": "返回最近多少条，默认20"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        from 运行诊断器 import 运行诊断器类
        诊断器 = 运行诊断器类._实例引用
        if not 诊断器:
            return 操作结果.失败("运行诊断器未初始化")
        未解决 = 参数.get("未解决Only", True)
        最近数 = 参数.get("最近数", 20)
        结果 = 诊断器.查询错误(未解决Only=未解决, 最近数=最近数)
        # 格式化为可读文本
        文本行 = []
        for e in 结果.get("错误列表", []):
            状态 = "✅已解决" if e.get("已解决") else "❌未解决"
            文本行.append(f"[{e['时间']}] {状态} | {e.get('异常类型','')} @ {e.get('来源','')} | {e.get('异常信息','')[:120]}")
        for w in 结果.get("警告列表", []):
            状态 = "✅已解决" if w.get("已解决") else "❌未解决"
            文本行.append(f"[{w['时间']}] ⚠️警告 {状态} @ {w.get('来源','')} | {w.get('信息','')[:120]}")
        统计 = 结果.get("统计", {})
        本次 = 结果.get("本次运行", {})
        文本 = f"统计: 总错误{统计.get('总错误',0)} 总警告{统计.get('总警告',0)} | 本次运行: 错误{本次.get('错误',0)} 警告{本次.get('警告',0)}\n"
        文本 += "\n".join(文本行) if 文本行 else "无错误记录"
        return 操作结果.成功(文本, {"错误数": len(结果.get("错误列表", [])), "警告数": len(结果.get("警告列表", []))})


