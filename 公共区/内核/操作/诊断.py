class 查询监控规则(操作基类):
    名称 = "查询监控规则"
    描述 = "查询所有监控规则及其触发情况(触发次数、最后触发时间)"
    参数结构 = {}

    def 执行(self, 参数: dict) -> 操作结果:
        from 运行诊断器 import 运行诊断器类
        诊断器 = 运行诊断器类._实例引用
        if not 诊断器:
            return 操作结果.失败("运行诊断器未初始化")
        结果 = 诊断器.查询规则()
        规则列表 = 结果.get("规则列表", [])
        if not 规则列表:
            return 操作结果.成功("无监控规则")
        文本行 = []
        for r in 规则列表:
            状态 = "启用" if r.get("启用") else "禁用"
            文本行.append(f"[{r['id']}] {状态} | {r['名称']} | 模块:{r.get('目标模块','-')} 函数:{r.get('目标函数','-')} 异常:{r.get('异常类型','-')} 关键字:{r.get('关键字','-')} | 触发:{r.get('触发次数',0)}次 最后:{r.get('最后触发','从未')}")
        return 操作结果.成功("\n".join(文本行), {"规则数": len(规则列表)})


