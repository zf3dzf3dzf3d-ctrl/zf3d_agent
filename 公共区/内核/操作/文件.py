class 替换文本(操作基类):
    名称 = "替换文本"
    描述 = "替换文件中的指定文本片段。默认只替换第一个匹配，可设置全部替换。匹配数>1时会警告"
    参数结构 = {
        "路径": {"类型": "字符串", "必填": True, "说明": "文件路径"},
        "旧文本": {"类型": "字符串", "必填": True, "说明": "要被替换的原始文本（必须与文件中内容完全一致）"},
        "新文本": {"类型": "字符串", "必填": True, "说明": "替换后的新文本"},
        "全部替换": {"类型": "布尔", "必填": False, "说明": "是否替换所有匹配项，默认false(只替换第一个)"},
        "匹配计数": {"类型": "布尔", "必填": False, "说明": "是否在替换前报告匹配数量，默认true"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        路径 = 参数.get("路径", "")
        旧文本 = 参数.get("旧文本", "")
        新文本 = 参数.get("新文本", "")
        全部替换 = 参数.get("全部替换", False)
        需计数 = 参数.get("匹配计数", True)
        if not 路径:
            return 操作结果.失败("路径为空")
        if not 旧文本:
            return 操作结果.失败("旧文本为空")
        if self.文件管理器:
            结果 = self.文件管理器.替换文本(路径, 旧文本, 新文本, AI调用=True, 全部替换=全部替换, 报告匹配数=需计数)
            if 结果.get("成功"):
                匹配数 = 结果.get("匹配数", 1)
                替换数 = 结果.get("替换数", 1)
                消息 = f"已替换 {替换数} 处 (共{匹配数}处匹配) {len(旧文本)}字→{len(新文本)}字" if 全部替换 else (
                    f"⚠️ 文件中有{匹配数}处匹配，已替换第1处。如需全部替换请设置全部替换=true\n已替换 {len(旧文本)}字 → {len(新文本)}字"
                    if 匹配数 > 1 and 需计数 else f"已替换 {len(旧文本)}字 → {len(新文本)}字"
                )
                return 操作结果.成功(消息, 元数据={
                    "操作类型": "替换文本", "路径": 路径,
                    "匹配数": 匹配数, "替换数": 替换数,
                    "旧文本长度": len(旧文本), "新文本长度": len(新文本)
                })
            else:
                错误 = 结果.get("错误", "替换失败")
                if 结果.get("匹配数", 0) > 0:
                    错误 += f" (找到{结果['匹配数']}处匹配)"
                return 操作结果.失败(错误)
        try:
            with open(路径, "r", encoding="utf-8") as f:
                内容 = f.read()
            匹配数 = 内容.count(旧文本)
            if 匹配数 == 0:
                return 操作结果.失败("未找到要替换的文本，请确保旧文本与文件中完全一致")
            if 匹配数 > 1 and not 全部替换 and 需计数:
                新内容 = 内容.replace(旧文本, 新文本, 1)
                with open(路径, "w", encoding="utf-8") as f:
                    f.write(新内容)
                return 操作结果.成功(f"⚠️ 文件中有{匹配数}处匹配，已替换第1处。如需全部替换请设置全部替换=true", 元数据={
                    "操作类型": "替换文本", "路径": 路径, "匹配数": 匹配数, "替换数": 1
                })
            if 全部替换:
                新内容 = 内容.replace(旧文本, 新文本)
                替换数 = 匹配数
            else:
                新内容 = 内容.replace(旧文本, 新文本, 1)
                替换数 = 1
            with open(路径, "w", encoding="utf-8") as f:
                f.write(新内容)
            return 操作结果.成功(f"已替换 {替换数} 处 {len(旧文本)}字 → {len(新文本)}字", 元数据={
                "操作类型": "替换文本", "路径": 路径, "匹配数": 匹配数, "替换数": 替换数
            })
        except FileNotFoundError:
            return 操作结果.失败(f"文件不存在: {路径}")
        except Exception as e:
            return 操作结果.失败(f"替换失败: {e}")


class 批量编辑(操作基类):
    名称 = "批量编辑"
    描述 = "对同一文件执行多处文本替换，一次调用完成多个修改。每个编辑项可单独设置全部替换"
    参数结构 = {
        "路径": {"类型": "字符串", "必填": True, "说明": "文件路径"},
        "编辑列表": {"类型": "字符串", "必填": True, "说明": "JSON数组，每项含旧文本/新文本/全部替换(可选)，如: [{\"旧文本\":\"a\",\"新文本\":\"b\"},{\"旧文本\":\"c\",\"新文本\":\"d\",\"全部替换\":true}]"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        路径 = 参数.get("路径", "")
        编辑列表字符串 = 参数.get("编辑列表", "")
        if not 路径:
            return 操作结果.失败("路径为空")
        if not 编辑列表字符串:
            return 操作结果.失败("编辑列表为空")
        try:
            编辑列表 = json.loads(编辑列表字符串) if isinstance(编辑列表字符串, str) else 编辑列表字符串
        except (json.JSONDecodeError, TypeError) as e:
            return 操作结果.失败(f"编辑列表JSON解析失败: {e}")
        if not isinstance(编辑列表, list) or len(编辑列表) == 0:
            return 操作结果.失败("编辑列表必须是非空数组")
        if self.文件管理器:
            结果 = self.文件管理器.批量替换(路径, 编辑列表, AI调用=True)
            if 结果.get("成功"):
                信息 = f"成功替换{结果.get('替换数', 0)}处"
                详情 = 结果.get("详情", [])
                if 详情:
                    for d in 详情:
                        if d.get("匹配数", 0) == 0:
                            信息 += f"\n  ⚠️ 「{d.get('旧文本','')[:30]}」未找到匹配"
                        elif d.get("匹配数", 1) > 1 and not d.get("全部替换"):
                            信息 += f"\n  ⚠️ 「{d.get('旧文本','')[:30]}」有{d['匹配数']}处匹配，仅替换第1处"
                失败项 = 结果.get("失败项")
                if 失败项:
                    信息 += f"，失败{len(失败项)}处"
                return 操作结果.成功(信息)
            return 操作结果.失败(结果.get("错误", "批量替换失败"))
        try:
            文件路径 = Path(路径)
            with open(文件路径, "r", encoding="utf-8") as f:
                内容 = f.read()
            成功数 = 0
            详情列表 = []
            for 编辑 in 编辑列表:
                旧文本 = 编辑.get("旧文本", "")
                新文本 = 编辑.get("新文本", "")
                项全部替换 = 编辑.get("全部替换", False)
                匹配数 = 内容.count(旧文本) if 旧文本 else 0
                if 匹配数 == 0:
                    详情列表.append({"旧文本": 旧文本[:30], "匹配数": 0})
                    continue
                if 项全部替换:
                    内容 = 内容.replace(旧文本, 新文本)
                    成功数 += 匹配数
                else:
                    内容 = 内容.replace(旧文本, 新文本, 1)
                    成功数 += 1
                详情列表.append({"旧文本": 旧文本[:30], "匹配数": 匹配数, "全部替换": 项全部替换})
            with open(文件路径, "w", encoding="utf-8") as f:
                f.write(内容)
            信息 = f"成功替换{成功数}处"
            for d in 详情列表:
                if d.get("匹配数", 0) == 0:
                    信息 += f"\n  ⚠️ 「{d['旧文本']}」未找到匹配"
                elif d.get("匹配数", 1) > 1 and not d.get("全部替换"):
                    信息 += f"\n  ⚠️ 「{d['旧文本']}」有{d['匹配数']}处匹配，仅替换第1处"
            return 操作结果.成功(信息)
        except Exception as e:
            return 操作结果.失败(f"批量编辑失败: {e}")


class 列出目录(操作基类):
    名称 = "列出目录"
    描述 = "列出目录下的文件和子目录"
    参数结构 = {
        "路径": {"类型": "字符串", "必填": False, "说明": "目录路径，默认当前目录"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        路径 = 参数.get("路径", "./")
        if self.文件管理器:
            结果 = self.文件管理器.列目录(路径)
            if 结果.get("成功"):
                内容列表 = 结果.get("内容", [])
                文件数 = sum(1 for i in 内容列表 if i.get("类型") == "文件")
                目录数 = sum(1 for i in 内容列表 if i.get("类型") == "目录")
                格式化 = []
                for 项 in 内容列表:
                    图标 = "📁" if 项.get("类型") == "目录" else "📄"
                    大小 = 项.get("大小", 0)
                    后缀 = f" ({大小}B)" if 大小 and 项.get("类型") == "文件" else ""
                    格式化.append(f"{图标} {项['名称']}{后缀}")
                return 操作结果.成功("\n".join(格式化) if 格式化 else "(空目录)", 元数据={
                    "操作类型": "列出目录", "路径": 路径,
                    "文件数": 文件数, "目录数": 目录数
                })
            else:
                return 操作结果.失败(结果.get("错误", "列出失败"))
        try:
            if not os.path.isdir(路径):
                return 操作结果.失败(f"不是目录: {路径}")
            条目 = os.listdir(路径)
            结果列表 = []
            for 条目名 in sorted(条目):
                完整路径 = os.path.join(路径, 条目名)
                类型 = "📁" if os.path.isdir(完整路径) else "📄"
                大小 = os.path.getsize(完整路径) if os.path.isfile(完整路径) else 0
                结果列表.append(f"{类型} {条目名}" + (f" ({大小}B)" if 大小 else ""))
            return 操作结果.成功("\n".join(结果列表) if 结果列表 else "(空目录)")
        except Exception as e:
            return 操作结果.失败(f"列出失败: {e}")


