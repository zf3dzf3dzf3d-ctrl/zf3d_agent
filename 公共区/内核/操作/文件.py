    def 执行(self, 参数: dict) -> 操作结果:
        if self.文件管理器:
            结果 = self.文件管理器.列出回收站()
            if 结果.get("成功"):
                内容 = 结果.get("内容", [])
                if not 内容:
                    return 操作结果.成功("回收站为空", 元数据={"操作类型": "列出回收站", "文件数": 0})
                格式化 = []
                for 项 in 内容:
                    大小MB = 项.get("大小", 0) / (1024 * 1024)
                    大小显示 = f"{大小MB:.1f}MB" if 大小MB >= 1 else f"{项.get('大小', 0)}B"
                    原路径 = 项.get("原始路径", "") or "未知"
                    格式化.append(f"🗑️ {项['回收站名']} ({大小显示})\n   原始名: {项['原始名']}\n   原路径: {原路径}\n   删除时间: {项['删除时间']}")
                return 操作结果.成功("\n".join(格式化), 元数据={
                    "操作类型": "列出回收站", "文件数": len(内容)
                })
            else:
                return 操作结果.失败(结果.get("错误", "列出失败"))
        return 操作结果.失败("文件管理器未初始化")


