class 创建文件(操作基类):
    名称 = "创建文件"
    描述 = "创建文件并写入内容"
    参数结构 = {
        "路径": {"类型": "字符串", "必填": True, "说明": "文件路径"},
        "内容": {"类型": "字符串", "必填": False, "说明": "文件内容，默认为空"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        路径 = 参数.get("路径", "")
        内容 = 参数.get("内容", "")
        if not 路径:
            return 操作结果.失败("路径为空")
        if self.文件管理器:
            if 内容:
                写结果 = self.文件管理器.写入文件(路径, 内容, AI调用=True)
                if 写结果.get("成功"):
                    return 操作结果.成功(f"已创建: {路径}")
                return 操作结果.失败(写结果.get("错误", "写入失败"))
            else:
                结果 = self.文件管理器.新建文件(路径, AI调用=True)
                if 结果.get("成功"):
                    return 操作结果.成功(f"已创建: {路径}")
                return 操作结果.失败(结果.get("错误", "创建失败"))
        try:
            Path(路径).parent.mkdir(parents=True, exist_ok=True)
            with open(路径, "w", encoding="utf-8") as f:
                f.write(内容)
            return 操作结果.成功(f"已创建: {路径}")
        except Exception as e:
            return 操作结果.失败(f"创建失败: {e}")


class 读取文件(操作基类):
    名称 = "读取文件"
    描述 = "读取文件内容，支持分页读取（offset起始行+limit行数），适用于大文件"
    参数结构 = {
        "路径": {"类型": "字符串", "必填": True, "说明": "文件路径"},
        "offset": {"类型": "整数", "必填": False, "说明": "从第N行开始读取(0-based)，默认0(从头开始)"},
        "limit": {"类型": "整数", "必填": False, "说明": "最多读取N行，0=读取全部，默认0"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        路径 = 参数.get("路径", "")
        offset = 参数.get("offset", 0)
        limit = 参数.get("limit", 0)
        if not 路径:
            return 操作结果.失败("路径为空")
        if self.文件管理器:
            结果 = self.文件管理器.读取文件(路径, AI调用=True)
            if 结果.get("成功"):
                内容 = 结果.get("内容", "")
                行列表 = 内容.split("\n")
                总行数 = len(行列表)
                if offset > 0 or limit > 0:
                    起始 = min(offset, 总行数)
                    结束 = min(offset + limit, 总行数) if limit > 0 else 总行数
                    分页内容 = "\n".join(行列表[起始:结束])
                    范围信息 = f"\n[文件共{总行数}行，已读取第{起始+1}-{结束}行]"
                    if 结束 < 总行数:
                        范围信息 += f"，下页offset={结束}"
                    return 操作结果.成功(分页内容 + 范围信息, 元数据={
                        "操作类型": "读取文件", "路径": 路径,
                        "总行数": 总行数, "返回行数": 结束-起始,
                        "下页偏移": 结束 if 结束 < 总行数 else None,
                        "文件大小KB": round(len(内容)/1024, 1)
                    })
                return 操作结果.成功(内容, 元数据={
                    "操作类型": "读取文件", "路径": 路径,
                    "总行数": 总行数, "返回行数": 总行数,
                    "文件大小KB": round(len(内容)/1024, 1)
                })
            else:
                return 操作结果.失败(结果.get("错误", "读取失败"))
        try:
            with open(路径, "r", encoding="utf-8") as f:
                行列表 = f.readlines()
            总行数 = len(行列表)
            if offset > 0 or limit > 0:
                起始 = min(offset, 总行数)
                结束 = min(offset + limit, 总行数) if limit > 0 else 总行数
                分页内容 = "".join(行列表[起始:结束])
                范围信息 = f"\n[文件共{总行数}行，已读取第{起始+1}-{结束}行]"
                if 结束 < 总行数:
                    范围信息 += f"，下页offset={结束}"
                return 操作结果.成功(分页内容 + 范围信息, 元数据={
                    "操作类型": "读取文件", "路径": 路径,
                    "总行数": 总行数, "返回行数": 结束-起始,
                    "下页偏移": 结束 if 结束 < 总行数 else None
                })
            return 操作结果.成功("".join(行列表), 元数据={
                "操作类型": "读取文件", "路径": 路径,
                "总行数": 总行数, "返回行数": 总行数
            })
        except FileNotFoundError:
            return 操作结果.失败(f"文件不存在: {路径}")
        except Exception as e:
            return 操作结果.失败(f"读取失败: {e}")


