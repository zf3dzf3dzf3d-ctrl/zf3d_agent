class 搜索Bug操作(操作基类):
    名称 = "搜索Bug"
    描述 = "按关键词全文搜索Bug库（问题描述、文件路径、修复说明）"
    参数结构 = {
        "关键词": {"类型": "字符串", "必填": True, "说明": "搜索关键词"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        from Bug追踪器 import Bug追踪器类
        追踪器 = Bug追踪器类._实例引用
        if not 追踪器:
            return 操作结果.失败("Bug追踪器未初始化")
        关键词 = 参数.get("关键词", "")
        if not 关键词:
            return 操作结果.失败("关键词不能为空")
        结果 = 追踪器.搜索Bug(关键词)
        搜索结果 = 结果.get("结果", [])
        if not 搜索结果:
            return 操作结果.成功(f"未找到匹配「{关键词}」的Bug")
        行 = [f"找到{len(搜索结果)}条匹配:"]
        for r in 搜索结果:
            状态 = "❌" if r.get("状态") == "未解决" else "✅"
            行.append(f"{状态} #{r['id']} {r['文件路径']} — {r['问题描述'][:80]}")
        return 操作结果.成功("\n".join(行), {"匹配数": len(搜索结果)})
