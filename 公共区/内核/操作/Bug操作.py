            问题描述=参数.get("问题描述", ""),
            行号=参数.get("行号", 0),
            严重程度=参数.get("严重程度", "中"),
            发现来源=参数.get("发现来源", "AI检查"),
            修复代码=参数.get("修复代码", "")
