"""
知识库操作 — LLM可调用的知识库管理操作
导入文档/搜索知识库/列出知识库文档/删除知识库文档
"""
import re
import urllib.request
from pathlib import Path
from .基类 import 操作结果, 操作基类

# 全局知识库实例引用
_知识库实例 = None


def 设置知识库实例(实例):
    global _知识库实例
    _知识库实例 = 实例


class 导入文档(操作基类):
    名称 = "导入文档"
    描述 = "导入文档到知识库（支持txt/md/html/json格式，或网页URL），自动分块并建立搜索索引"
    参数结构 = {
        "文档名": {"类型": "字符串", "必填": True, "说明": "文档名称（不含路径）"},
        "内容": {"类型": "字符串", "必填": False, "说明": "文档文本内容（与网址二选一）"},
        "网址": {"类型": "字符串", "必填": False, "说明": "网页URL，自动抓取内容导入"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _知识库实例:
            return 操作结果.失败("知识库未初始化")
        文档名 = 参数.get("文档名", "")
        内容 = 参数.get("内容", "")
        网址 = 参数.get("网址", "")
        if not 文档名:
            return 操作结果.失败("文档名为空")
        if not 内容 and not 网址:
            return 操作结果.失败("请提供文档内容或网址")
        # 从网页抓取
        if 网址:
            try:
                请求 = urllib.request.Request(网址, headers={"User-Agent": "Mozilla/5.0"})
                响应 = urllib.request.urlopen(请求, timeout=30)
                原始 = 响应.read().decode("utf-8", errors="replace")
                # 简易HTML转文本
                内容 = re.sub(r'<script[^>]*>.*?</script>', '', 原始, flags=re.DOTALL)
                内容 = re.sub(r'<style[^>]*>.*?</style>', '', 内容, flags=re.DOTALL)
                内容 = re.sub(r'<[^>]+>', '\n', 内容)
                内容 = re.sub(r'\n{3,}', '\n\n', 内容).strip()
                if not 内容:
                    return 操作结果.失败("网页内容为空")
            except Exception as e:
                return 操作结果.失败(f"网页抓取失败: {e}")
        # 导入
        结果 = _知识库实例.导入文档(文档名, 内容, 来源=网址 or "手动导入")
        if 结果.get("成功"):
            return 操作结果.成功(
                f"已导入文档 [{文档名}]，分块 {结果['块数']} 块",
                元数据=结果
            )
        return 操作结果.失败(结果.get("错误", "导入失败"))


class 搜索知识库(操作基类):
    名称 = "搜索知识库"
    描述 = "在知识库中搜索与关键词相关的文档片段"
    参数结构 = {
        "关键词": {"类型": "字符串", "必填": True, "说明": "搜索关键词或问题"},
        "数量": {"类型": "整数", "必填": False, "说明": "返回结果数量（默认3）"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _知识库实例:
            return 操作结果.失败("知识库未初始化")
        关键词 = 参数.get("关键词", "")
        if not 关键词:
            return 操作结果.失败("关键词为空")
        数量 = 参数.get("数量", 3)
        结果 = _知识库实例.召回(关键词, top_k=数量)
        if not 结果:
            return 操作结果.成功("未找到相关文档")
        格式化 = []
        for 项 in 结果:
            格式化.append(f"【{项['文档名']}】(相关度:{项['得分']}):\n{项['内容'][:300]}")
        return 操作结果.成功("\n\n---\n\n".join(格式化), 元数据={"结果数": len(结果)})


class 列出知识库文档(操作基类):
    名称 = "列出知识库文档"
    描述 = "查看知识库中已导入的文档列表"
    参数结构 = {}

    def 执行(self, 参数: dict) -> 操作结果:
        if not _知识库实例:
            return 操作结果.失败("知识库未初始化")
        文档列表 = _知识库实例.列出文档()
        if not 文档列表:
            return 操作结果.成功("知识库为空，请先导入文档")
        格式化 = [f"  {d['文档名']} ({d['块数']}块)" for d in 文档列表]
        return 操作结果.成功(f"共 {len(文档列表)} 个文档:\n" + "\n".join(格式化))


class 删除知识库文档(操作基类):
    名称 = "删除知识库文档"
    描述 = "从知识库中删除指定文档"
    参数结构 = {
        "文档名": {"类型": "字符串", "必填": True, "说明": "要删除的文档名称"}
    }

    def 执行(self, 参数: dict) -> 操作结果:
        if not _知识库实例:
            return 操作结果.失败("知识库未初始化")
        文档名 = 参数.get("文档名", "")
        if not 文档名:
            return 操作结果.失败("文档名为空")
        结果 = _知识库实例.删除文档(文档名)
        if 结果.get("成功"):
            return 操作结果.成功(f"已删除文档 [{文档名}]")
        return 操作结果.失败("删除失败")
