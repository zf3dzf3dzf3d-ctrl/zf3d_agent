    def _自动验证代码(self, 操作名, 操作参数, 执行成功, 操作注册中心):
        """文件修改后自动验证语法"""
        if not 执行成功:
            return ""
        文件修改操作 = {"写入文件", "替换文本", "批量编辑", "创建文件", "追加文件"}
        if 操作名 not in 文件修改操作:
            return ""
        文件路径 = 操作参数.get("路径", "")
        if not 文件路径:
            return ""
        后缀 = Path(文件路径).suffix.lower()
        if 后缀 not in (".py", ".json"):
            return ""
        验证结果 = 操作注册中心.执行("验证代码", {"路径": 文件路径})
        if 验证结果.get("成功", False):
            return f"\n[自动验证] ✅ {验证结果.get('数据', '')}"
        else:
            错误 = 验证结果.get('错误', '验证失败')
            if 后缀 == ".py":
                return f"\n[自动验证] ⚠️ Python语法错误:\n{错误}\n请立即修复此语法错误！"
            else:
                return f"\n[自动验证] ⚠️ JSON格式错误:\n{错误}\n请修复JSON格式！"

    def _解析LLM输出(self, 内容):
        """解析LLM输出，判断是最终回复还是操作调用"""
        import re

        # 策略1: ```json ... ``` 代码块
        json块 = self._提取JSON块(内容)
        if json块:
            结果 = self._尝试解析操作(json块)
            if 结果:
                return 结果

        # 策略2: 直接解析整段
        结果 = self._尝试解析操作(内容.strip())
        if 结果:
            return 结果

        # 策略3: 第一个{ 到最后一个}
        start = 内容.find('{')
        if start != -1:
            end = 内容.rfind('}')
            if end > start:
                结果 = self._尝试解析操作(内容[start:end + 1])
                if 结果:
                    return 结果

        # 策略4: 逐个代码块
        for 匹配 in re.finditer(r'```(?:json)?\s*\n?(.*?)\n?\s*```', 内容, re.DOTALL):
            结果 = self._尝试解析操作(匹配.group(1).strip())
            if 结果:
                return 结果

        return {"类型": "最终回复", "内容": 内容}

    def _尝试解析操作(self, json字符串):
        """尝试解析JSON为操作调用"""
        try:
            数据 = json.loads(json字符串)
            if isinstance(数据, dict) and "操作" in 数据:
                return {
                    "类型": "操作调用", "操作名": 数据["操作"],
                    "参数": 数据.get("参数", {}),
                    "思考": 数据.get("思考", ""), "原始内容": json字符串
                }
        except (json.JSONDecodeError, TypeError):
            pass
        return None

    def _提取JSON块(self, 文本):
        """提取```json ... ```代码块"""
        import re
        匹配 = re.search(r'```(?:json)?\s*\n?(.*?)\n?\s*```', 文本, re.DOTALL)
        if 匹配:
            return 匹配.group(1).strip()
        return ""

    # ============ 推理流 ============
    def _推入推理流(self, 类型, 内容):
        with self._推理流锁:
            self.推理流.append({
                "类型": 类型, "内容": 内容,
                "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            })
            if self._推理流回调:
                self._推理流回调(类型, 内容)

    def _重置推理流(self):
        with self._推理流锁:
            self.推理流 = []
            self.推理流索引 = 0

    def 获取推理流(self, 上次索引=0):
        with self._推理流锁:
            新记录 = self.推理流[上次索引:]
            return {"成功": True, "记录": 新记录, "当前索引": len(self.推理流)}

    def 取消(self):
        self._取消标志 = True
        self._重置推理流()
