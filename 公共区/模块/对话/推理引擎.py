    def _执行工具调用(self, 工具调用列表, 步数, 推理过程, 上下文管理器,
                      操作注册中心, 文件上下文, 结果, 意图, 对话历史, 模型直连器):
        """执行FC模式的工具调用

        多个工具调用时并行执行无依赖的操作（如同时读取多个文件）
        """
        思考提示 = 结果.get("回复内容", "") or ""
        原始助手消息 = 结果.get("原始响应", {}).get("choices", [{}])[0].get("message", {})

        # 保存原始assistant消息到轨迹
        if 原始助手消息 and 原始助手消息.get("tool_calls"):
            上下文管理器.添加_步骤(assistant消息=原始助手消息)

        # === 阶段1: 解析所有调用，收集可并行的操作 ===
        待执行 = []  # [(调用, 操作名, 操作参数, 思考文本), ...]
        拦截结果 = []  # 被框选拦截的操作直接记录
        思考提示_剩余 = 思考提示
        for 调用 in 工具调用列表:
            英文名 = 调用.get("名称", "")
            英文参数 = 调用.get("参数", {})
            操作名, 操作参数 = 操作注册中心.解析工具调用(英文名, 英文参数)

            if 思考提示_剩余:
                思考文本 = 思考提示_剩余
                思考提示_剩余 = ""
            else:
                参数摘要 = ", ".join(f"{k}={str(v)[:30]}" for k, v in 操作参数.items() if v)
                思考文本 = f"调用{操作名}({参数摘要})"

            # 框选文本拦截
            有框选 = 文件上下文 and "框选" in 文件上下文
            if 有框选 and 操作名 in ("读取文件", "读文件", "读取", "写入文件", "写文件"):
                拦截提示 = f"❌ 禁止使用「{操作名}」！当前有框选文本，必须使用「替换文本」操作！"
                推理过程.append({
                    "步骤": 步数, "类型": "操作", "操作": 操作名,
                    "参数": 操作参数, "思考": 思考文本, "结果": 拦截提示,
                    "成功": False, "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
                })
                上下文管理器.添加_步骤(tool消息={
                    "role": "tool", "tool_call_id": 调用.get("id", ""), "content": 拦截提示
                })
                拦截结果.append(拦截提示)
                continue

            待执行.append((调用, 操作名, 操作参数, 思考文本))

        # === 阶段2: 执行操作（多个时并行） ===
        import concurrent.futures

        def _执行单个(调用_操作名_参数_思考):
            调用, 操作名, 操作参数, 思考文本 = 调用_操作名_参数_思考
            # 只读模式权限拦截
            工作模式 = self._当前工具上下文.get("agent", "商量")
            if 工作模式 == "只读" and not 操作注册中心.检查操作权限(操作名, 工作模式):
                执行结果 = {"成功": False, "错误": f"只读模式下禁止执行「{操作名}」操作"}
            else:
                执行结果 = 操作注册中心.执行(操作名, 操作参数, 上下文={**self._当前工具上下文, "步数": 步数})
            return 调用, 操作名, 操作参数, 思考文本, 执行结果

        执行结果列表 = []
        if len(待执行) == 1:
            执行结果列表 = [_执行单个(待执行[0])]
        elif len(待执行) > 1:
            # 并行执行（最多4个并发，防止资源滥用）
            with concurrent.futures.ThreadPoolExecutor(max_workers=min(4, len(待执行))) as 池:
                执行结果列表 = list(池.map(_执行单个, 待执行))

        # === 阶段3: 处理结果（按原始顺序，保证上下文一致性） ===
        当前观察 = ""
        拦截观察 = ""
        if 拦截结果:
            拦截观察 = 拦截结果[-1]

        for 调用, 操作名, 操作参数, 思考文本, 执行结果 in 执行结果列表:
            操作成功 = 执行结果.get("成功", False)
            操作结果文本 = 执行结果.get("数据", "") if 操作成功 else 执行结果.get("错误", "")
            操作结果文本 = self._截断操作结果(操作名, 操作结果文本, 步数)

            推理过程.append({
                "步骤": 步数, "类型": "操作", "操作": 操作名,
                "参数": 操作参数, "思考": 思考文本, "fc": True,
                "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            })
            self._推入推理流("操作调用", {"操作": 操作名, "参数": 操作参数, "思考": 思考文本[:200]})

