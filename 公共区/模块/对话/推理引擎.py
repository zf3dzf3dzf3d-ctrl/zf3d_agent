    def _自动验证代码(self, 操作名, 操作参数, 执行成功, 操作注册中心, 执行结果=None):
        """执行后自动校验：命令返回码、网页内容、文件语法、变更摘要"""
        if not 执行成功:
            return ""
        结果信息 = ""
        元数据 = (执行结果 or {}).get("元数据", {})

        # 1. 命令返回码检测
        if 操作名 == "运行命令":
            退出码 = 元数据.get("退出码", 0)
            if 退出码 != 0:
                结果信息 += f"\n[自动校验] ⚠️ 命令退出码={退出码}（非0表示失败），请检查输出中的错误信息"

        # 2. 网页内容检查
        if 操作名 in ("网页抓取", "网络搜索"):
            数据 = (执行结果 or {}).get("数据", "")
            if 数据 and len(数据) < 50:
                结果信息 += f"\n[自动校验] ⚠️ 返回内容过短({len(数据)}字符)，可能抓取失败或页面为空"

        # 3. 文件修改：变更摘要 + 语法验证
        文件修改操作 = {"写入文件", "替换文本", "批量编辑", "创建文件", "追加文件"}
        if 操作名 in 文件修改操作:
            文件路径 = 操作参数.get("路径", "")
            if 文件路径:
                # 变更摘要
                if 操作名 == "替换文本":
                    替换数 = 元数据.get("替换数", 0)
                    匹配数 = 元数据.get("匹配数", 0)
                    结果信息 += f"\n[变更] 替换{替换数}/{匹配数}处"
                elif 操作名 == "批量编辑":
                    编辑数 = len(操作参数.get("编辑列表", []))
                    if isinstance(操作参数.get("编辑列表"), str):
                        try:
                            编辑数 = len(json.loads(操作参数["编辑列表"]))
                        except Exception:
                            编辑数 = 0
                    结果信息 += f"\n[变更] 批量编辑{编辑数}项"
                elif 操作名 in ("写入文件", "创建文件"):
                    行数 = 元数据.get("内容行数", 0)
                    if 行数 == 0:
                        内容 = 操作参数.get("内容", "")
                        行数 = 内容.count("\n") + 1 if 内容 else 0
                    动作 = "新建" if 操作名 == "创建文件" else "写入"
                    结果信息 += f"\n[变更] {动作}{行数}行"
                elif 操作名 == "追加文件":
                    内容 = 操作参数.get("内容", "")
                    行数 = 内容.count("\n") + 1 if 内容 else 0
                    结果信息 += f"\n[变更] 追加{行数}行"

                # 语法验证
                后缀 = Path(文件路径).suffix.lower()
                if 后缀 in (".py", ".json"):
                    验证结果 = 操作注册中心.执行("验证代码", {"路径": 文件路径})
                    if 验证结果.get("成功", False):
                        结果信息 += f"\n[自动校验] ✅ {验证结果.get('数据', '')}"
                    else:
                        错误 = 验证结果.get('错误', '验证失败')
                        if 后缀 == ".py":
                            结果信息 += f"\n[自动校验] ⚠️ Python语法错误:\n{错误}\n请立即修复此语法错误！"
                        else:
                            结果信息 += f"\n[自动校验] ⚠️ JSON格式错误:\n{错误}\n请修复JSON格式！"

        return 结果信息

    def _检测重复操作(self, 推理过程, 签名历史, 步数):
        """检测ReAct循环中的重复操作

        返回 None 表示无重复，否则返回 dict:
        - {"动作": "警告", "消息": "..."} 注入提示让AI改变策略
        - {"动作": "循环", "消息": "...", "判定": "疑似循环"} 需导师介入
        - {"动作": "循环", "消息": "...", "判定": "死循环"} 需导师介入，导师用完才终止
        """
        if not 推理过程:
            return None
        最后一步 = 推理过程[-1]
        if 最后一步.get("类型") != "操作":
            return None
        操作名 = 最后一步.get("操作", "")
        操作参数 = 最后一步.get("参数", {})
        # 生成操作签名：操作名 + 参数的关键字段
        参数摘要 = json.dumps(操作参数, sort_keys=True, ensure_ascii=False)[:500]
        签名 = f"{操作名}:{hash(参数摘要)}"
        签名历史.append(签名)
        # 只检查最近10步内的重复
        窗口 = 签名历史[-10:]
        重复次数 = 窗口.count(签名)
        if 重复次数 >= 5:
            return {
                "动作": "循环",
                "判定": "死循环",
                "消息": f"操作「{操作名}」已重复执行{重复次数}次。"
            }
        if 重复次数 >= 3:
            return {
                "动作": "警告",
                "消息": (
                    f"⚠️ 重复检测：操作「{操作名}」已重复执行{重复次数}次。\n"
                    f"你已经执行过这个操作了，请不要重复相同操作。\n"
                    f"如果之前的操作结果不满意，请换一种方法或调整参数。\n"
                    f"如果任务已完成，请直接输出最终回复，不要再调用操作。"
                )
            }
        return None

    def _ai审核员审核(self, 推理过程, 步数, 上次审核步数, 用户消息, 模型直连器):
        """AI审核员：动态间隔审核推理轨迹，判断是否死循环或任务已完成

        审核间隔根据推理状态动态调整：
        - 顺利推进（不同操作+成功）：间隔拉长到7步（省token）
        - 有失败：间隔缩短到3-4步（更警惕）
        - 长任务（>15步）：间隔上限3步（防失控）
        - 前3步不审核（起步阶段太早）

        返回 None 表示无需干预，否则返回判定结果:
        - {"判定": "疑似循环"} 有重复迹象，需导师介入换思路
        - {"判定": "死循环"} 明确死循环，需导师介入换思路
        - {"判定": "任务完成"} 任务已实质完成，应停止继续操作
        导师介入/终止逻辑由调用方（执行方法）负责
        """
        # 前3步不审核（起步阶段）
        if 步数 < 4:
            return None
        if not 模型直连器:
            return None
        if len(推理过程) < 4:
            return None

        # === 动态计算审核间隔 ===
        审核间隔 = 5  # 基础间隔
        最近步骤 = 推理过程[-10:]
        操作步骤 = [s for s in 最近步骤 if s.get("类型") == "操作"]
        失败数 = sum(1 for s in 操作步骤 if not s.get("成功", True))

        # 有失败 → 缩短间隔（更频繁检查）
        if 失败数 >= 2:
            审核间隔 = 3
        elif 失败数 >= 1:
            审核间隔 = 4

        # 长任务 → 更频繁检查（长时间运行更容易失控）
        if 步数 > 25:
            审核间隔 = min(审核间隔, 2)
        elif 步数 > 15:
            审核间隔 = min(审核间隔, 3)

        # 最近3步都是不同操作且都成功 → 拉长间隔（任务进展顺利，省token）
        最近3操作 = 操作步骤[-3:] if len(操作步骤) >= 3 else []
        if (len(最近3操作) >= 3
                and len(set(s.get("操作", "") for s in 最近3操作)) == 3
                and all(s.get("成功") for s in 最近3操作)):
            审核间隔 = max(审核间隔, 7)

        if 步数 - 上次审核步数 < 审核间隔:
            return None

        # 构建推理轨迹摘要
        轨迹摘要 = []
        for step in 推理过程[-10:]:
            操作名 = step.get("操作", step.get("类型", ""))
            思考 = step.get("思考", "")[:80]
            结果摘要 = str(step.get("结果", ""))[:80]
            成功 = "✓" if step.get("成功") else "✗"
            轨迹摘要.append(f"  步{step.get('步骤','')}: [{操作名}] {成功} {思考} → {结果摘要}")
        轨迹文本 = "\n".join(轨迹摘要)

        审核提示词 = (
            "你是AI审核员，负责判断ReAct推理循环的状态。\n"
            "分析以下推理轨迹，判断AI当前处于什么状态。\n\n"
            f"用户原始需求: {用户消息[:200]}\n"
            f"当前已执行步数: {步数}\n"
            f"最近推理轨迹:\n{轨迹文本}\n\n"
            "只回复一个词：\n"
            "- 进展中：AI在推进任务，没有重复模式\n"
            "- 疑似循环：有重复迹象但不确定，需要提醒\n"
            "- 死循环：AI明确在重复相同模式，没有实质进展\n"
            "- 任务完成：用户的需求已经满足，AI应该停止调用工具，直接输出总结"
        )

        try:
            结果 = 模型直连器.发送消息(
                [{"role": "user", "content": 审核提示词}],
                "你是AI审核员。只回复一个词：进展中/疑似循环/死循环/任务完成。不要解释。"
            )
            if not 结果.get("成功"):
                return None
            判定 = 结果.get("回复内容", "").strip()
            if "任务完成" in 判定 or "完成" in 判定:
                self._推入推理流("审核员", {"判定": "任务完成", "步数": 步数})
                return {"判定": "任务完成"}
            if "死循环" in 判定:
                self._推入推理流("审核员", {"判定": "死循环", "步数": 步数})
                return {"判定": "死循环"}
            elif "疑似" in 判定 or "循环" in 判定:
                self._推入推理流("审核员", {"判定": "疑似循环", "步数": 步数})
                return {"判定": "疑似循环"}
        except Exception:
            pass
        return None

    def _解析LLM输出(self, 内容):
        """解析LLM输出，判断是最终回复还是操作调用"""
        import re

        # 策略1: ```json ... ``` 代码块
        json块 = self._提取JSON块(内容)
        if json块:
            结果 = self._尝试解析操作(json块)
            if 结果:
                return 结果

        # 策略2: 直接解析整段
        结果 = self._尝试解析操作(内容.strip())
        if 结果:
            return 结果

        # 策略3: 第一个{ 到最后一个}
        start = 内容.find('{')
        if start != -1:
            end = 内容.rfind('}')
            if end > start:
                结果 = self._尝试解析操作(内容[start:end + 1])
                if 结果:
                    return 结果

        # 策略4: 逐个代码块
        for 匹配 in re.finditer(r'```(?:json)?\s*\n?(.*?)\n?\s*```', 内容, re.DOTALL):
            结果 = self._尝试解析操作(匹配.group(1).strip())
            if 结果:
                return 结果

        return {"类型": "最终回复", "内容": 内容}

    def _尝试解析操作(self, json字符串):
        """尝试解析JSON为操作调用"""
        try:
            数据 = json.loads(json字符串)
            if isinstance(数据, dict) and "操作" in 数据:
                return {
                    "类型": "操作调用", "操作名": 数据["操作"],
                    "参数": 数据.get("参数", {}),
                    "思考": 数据.get("思考", ""), "原始内容": json字符串
                }
        except (json.JSONDecodeError, TypeError):
            pass
        return None

    def _提取JSON块(self, 文本):
        """提取```json ... ```代码块"""
        import re
        匹配 = re.search(r'```(?:json)?\s*\n?(.*?)\n?\s*```', 文本, re.DOTALL)
        if 匹配:
            return 匹配.group(1).strip()
        return ""

    # ============ 推理流 ============
    def _推入推理流(self, 类型, 内容):
        with self._推理流锁:
            self.推理流.append({
                "类型": 类型, "内容": 内容,
                "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            })
            if self._推理流回调:
                self._推理流回调(类型, 内容)

    def _重置推理流(self):
        with self._推理流锁:
            self.推理流 = []
            self.推理流索引 = 0

    def 获取推理流(self, 上次索引=0):
        with self._推理流锁:
            新记录 = self.推理流[上次索引:]
            return {"成功": True, "记录": 新记录, "当前索引": len(self.推理流)}

    def 取消(self):
        self._取消标志 = True
        if hasattr(self, '_设置取消事件'):
            self._设置取消事件()
        self._重置推理流()
