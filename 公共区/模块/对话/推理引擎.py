"""推理引擎 — 状态机驱动的ReAct循环

职责：
1. 状态机驱动的推理循环（思考→行动→观察→校验→终止）
2. FC模式 + 文本模式 + 智能降级
3. 批量操作结构化控制（替代文字哄骗）
4. 集成错误分类恢复
5. 检查点保存 + 推理流推送

核心改进：
- 状态机替代while循环，每个状态独立处理
- 批量操作通过意图参数控制，不靠改写观察文字
- 降级检查基于错误类型分类，不靠字符串匹配
- 支持并行工具调用（多个tool_calls同时执行）
"""
import sys
import json
import time
import threading
from pathlib import Path
from datetime import datetime

内核目录 = Path(__file__).parent.parent.parent / "内核"
sys.path.insert(0, str(内核目录))


class 推理引擎类:
    """状态机驱动的推理引擎"""

    # 状态常量
    思考 = "思考"
    行动 = "行动"
    观察 = "观察"
    校验 = "校验"
    终止 = "终止"

    def __init__(self):
        self.最大步数 = 99
        self.超时秒数 = 30
        self._取消标志 = False
        self.推理流 = []
        self.推理流索引 = 0
        self._推理流锁 = threading.Lock()
        self._流式回调 = None

    def 设置回调(self, 推理流回调=None, 取消检查=None, 检查点回调=None, 流式回调=None):
        """设置外部回调"""
        self._推理流回调 = 推理流回调
        self._取消检查 = 取消检查
        self._检查点回调 = 检查点回调
        self._流式回调 = 流式回调

    def _可取消LLM调用(self, 模型直连器, 消息列表, 系统提示词, 工具列表=None, 工具选择=None, 超时秒=30):
        """在独立线程中执行LLM调用，期间检查取消标志，避免长时间阻塞"""
        结果容器 = [None]
        异常容器 = [None]
        def _调用():
            try:
                结果容器[0] = 模型直连器.发送消息(
                    消息列表, 系统提示词, 工具列表=工具列表, 工具选择=工具选择)
            except Exception as e:
                异常容器[0] = e
        线程 = threading.Thread(target=_调用, daemon=True)
        线程.start()
        等待间隔 = 0.2
        已等待 = 0
        while 线程.is_alive():
            线程.join(timeout=等待间隔)
            已等待 += 等待间隔
            # 取消检查
            if self._取消标志 or (self._取消检查 and self._取消检查()):
                return {"成功": False, "错误": "用户已取消", "已取消": True}
            # 超时保护
            if 已等待 >= 超时秒 + 5:
                return {"成功": False, "错误": "LLM调用超时", "超时": True}
        if 异常容器[0]:
            return {"成功": False, "错误": str(异常容器[0])}
        return 结果容器[0] or {"成功": False, "错误": "LLM返回空结果"}

    def 执行(self, 用户消息: str, 意图: dict, 对话历史: list,
             上下文管理器, 提示词构建器, 模型直连器, 操作注册中心,
             文件上下文: str = "", 永久记忆: list = None,
             配置: dict = None) -> dict:
        """执行推理循环

        参数:
            用户消息: 用户原始消息
            意图: 意图解析结果
            对话历史: 对话历史列表（会被修改）
            上下文管理器: 上下文管理器实例
            提示词构建器: 提示词构建器实例
            模型直连器: LLM直连器
            操作注册中心: 操作注册中心
            文件上下文: 前端注入的文件上下文
            永久记忆: 永久记忆列表
            配置: 模块配置
        """
        from 反思评估器 import 反思评估器类
        from 配置加载器 import 全局事件中心

        配置 = 配置 or {}
        反思器 = 反思评估器类()
        步数 = 0
        连续失败 = 0
        当前观察 = f"用户说: {用户消息}"
        推理过程 = []
        最终回复 = ""
        llm调用记录 = []
        _已确认无剩余 = False
        _查看操作计数 = 0

        # FC配置
        fc配置 = 模型直连器.配置.get("function_calling", {}) if 模型直连器 else {}
        fc启用 = fc配置.get("启用", False)
        工具列表 = 操作注册中心.获取工具定义() if (fc启用 and 操作注册中心) else None
        工具选择 = fc配置.get("tool_choice", "auto") if fc启用 else None
        fc已降级 = False
        _fc降级重试次数 = 0

        # 重置推理流和上下文
        上下文管理器.重置本轮轨迹()
        提示词构建器.刷新记忆缓存()
        self._取消标志 = False
        self._重置推理流()
        self._推入推理流("开始", {"消息": 用户消息[:200]})

        # 压缩历史
        if len(对话历史) > 上下文管理器.压缩阈值:
            对话历史[:] = 上下文管理器.压缩历史(对话历史, 模型直连器)

        while 步数 < self.最大步数:
            # === 状态: 取消检查 ===
            if self._取消标志 or (self._取消检查 and self._取消检查()):
                最终回复 = f"⏹ 用户已取消任务。"
                if 推理过程:
                    最终回复 += f" 已执行{步数}步。"
                对话历史.append({"角色": "助手", "内容": 最终回复,
                                  "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
                self._推入推理流("最终回复", {"内容": 最终回复})
                break

            步数 += 1
            self._推入推理流("思考", {"步数": 步数, "内容": f"第{步数}步推理"})

            # === 状态: 校验（连续失败检查） ===
            if 连续失败 >= 3:
                最终回复 = f"任务执行遇到困难，已连续失败{连续失败}次。当前进度：{步数 - 1}步。"
                if 推理过程:
                    最后操作 = 推理过程[-1].get("操作", "")
                    最后错误 = 推理过程[-1].get("结果", "")
                    最终回复 += f"\n最后尝试的操作: {最后操作}\n错误信息: {最后错误[:200]}"
                最终回复 += "\n建议：换一种方法，或提供更多上下文。"
                对话历史.append({"角色": "助手", "内容": 最终回复,
                                  "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
                break

            # === 状态: 思考（构建消息+调用LLM） ===
            消息列表 = 上下文管理器.构建_消息列表(对话历史)
            系统提示词 = 提示词构建器.构建(
                基础提示词=配置.get("系统提示词", ""),
                工作模式=配置.get("工作模式", "商量"),
                文件上下文=文件上下文,
                模型直连器=模型直连器,
                操作注册中心=操作注册中心,
                模块注册=配置.get("模块注册", {}),
                永久记忆=永久记忆 or [],
                配置={**配置, "当前消息": 用户消息, "新对话标志": 配置.get("新对话标志", False)},
                fc已降级=fc已降级
            )

            实际工具 = None if fc已降级 else 工具列表
            实际选择 = None if fc已降级 else 工具选择

            # 流式回调：将token推入推理流供前端实时显示
            def _流式token回调(片段):
                self._推入推理流("流式回复", {"内容": 片段})

            # 尝试流式调用（非工具调用时），失败则降级到普通调用
            使用流式 = hasattr(模型直连器, '发送消息流式') and self._流式回调 is not False
            if 使用流式:
                结果 = 模型直连器.发送消息流式(
                    消息列表, 系统提示词, 工具列表=实际工具, 工具选择=实际选择,
                    流式回调=_流式token回调
                )
                # 如果流式失败（非内容问题），降级到可取消普通调用
                if not 结果.get("成功") and "stream" in 结果.get("错误", "").lower():
                    结果 = self._可取消LLM调用(
                        模型直连器, 消息列表, 系统提示词,
                        工具列表=实际工具, 工具选择=实际选择, 超时秒=self.超时秒数)
            else:
                结果 = self._可取消LLM调用(
                    模型直连器, 消息列表, 系统提示词,
                    工具列表=实际工具, 工具选择=实际选择, 超时秒=self.超时秒数)

            # 记录LLM调用
            llm调用记录.append({
                "步骤": 步数,
                "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                "成功": 结果.get("成功", False),
                "原始请求": 结果.get("原始请求"),
                "原始响应": 结果.get("原始响应"),
                "耗时毫秒": 结果.get("耗时毫秒", 0)
            })

            # 取消检查
            if self._取消标志 or (self._取消检查 and self._取消检查()):
                最终回复 = f"⏹ 用户已取消任务。"
                if 推理过程:
                    最终回复 += f" 已执行{步数 - 1}步。"
                对话历史.append({"角色": "助手", "内容": 最终回复,
                                  "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
                self._推入推理流("最终回复", {"内容": 最终回复})
                break

            # === 状态: 校验（LLM调用结果检查） ===
            if not 结果.get("成功", False):
                错误信息 = 结果.get('错误', '')
                错误类型 = 反思器.分类错误(错误信息)
                策略 = 反思器.建议_恢复策略(错误类型, 连续失败)

                # 记录到运行诊断器
                try:
                    from 运行诊断器 import 运行诊断器类
                    if 运行诊断器类._实例引用:
                        运行诊断器类._实例引用.记录错误(
                            "对话.ReAct循环", 异常对象=None,
                            异常类型="模型调用失败", 异常信息=错误信息)
                except Exception:
                    pass

                if 策略["策略"] == "降级" and not fc已降级:
                    print(f"  ⚠️ FC降级: {策略['提示']}")
                    fc已降级 = True
                    步数 -= 1
                    continue
                elif 策略["策略"] == "重试" and 策略["可恢复"]:
                    if 策略["退避秒"] > 0:
                        time.sleep(策略["退避秒"])
                    步数 -= 1
                    连续失败 += 1
                    continue
                else:
                    # 终止
                    友好错误 = 反思器.友好化错误(错误信息)
                    错误回复 = f"❌ {友好错误}"
                    对话历史.append({"角色": "助手", "内容": 错误回复,
                                      "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
                    self._推入推理流("最终回复", {"内容": 错误回复, "步数": 步数})
                    return {"成功": False, "错误": 友好错误,
                            "回复": 错误回复, "推理过程": 推理过程,
                            "完整推理过程": 推理过程, "llm调用记录": llm调用记录, "步数": 步数}

            # === 状态: 行动（处理LLM响应） ===
            回复内容 = 结果.get("回复内容", "")
            工具调用列表 = 结果.get("工具调用", [])

            # FC模式：处理工具调用
            if 工具调用列表:
                当前观察 = self._执行工具调用(
                    工具调用列表, 步数, 推理过程, 上下文管理器,
                    操作注册中心, 文件上下文, 结果, 意图, 对话历史, 模型直连器)
                # 更新连续失败计数
                最后一步 = 推理过程[-1] if 推理过程 else None
                if 最后一步 and not 最后一步.get("成功", False):
                    连续失败 += 1
                else:
                    连续失败 = 0
                # 批量操作进度检查
                当前观察 = self._检查批量进度(
                    当前观察, 意图, 推理过程, 对话历史, 回复内容)
                # 批量文件操作：AI调用列出目录检查剩余文件后，允许下一次最终回复
                if 意图.get("类型") == "批量任务" and 意图.get("操作类型") == "文件操作":
                    最后操作 = 推理过程[-1].get("操作", "") if 推理过程 else ""
                    if 最后操作 == "列出目录":
                        _已确认无剩余 = True
                # 检查点保存
                if self._检查点回调:
                    self._检查点回调(步数, 当前观察, 推理过程)
                continue

            # 无工具调用：检查是否是"调用操作:"文字（FC模式下未正确使用tool_calls）
            if "调用操作:" in 回复内容 and _fc降级重试次数 < 2:
                _fc降级重试次数 += 1
                if not fc已降级:
                    print(f"  ⚠️ AI输出'调用操作:'文字但未使用tool_calls，自动降级")
                    fc已降级 = True
                提醒 = ("⚠️ 你刚才输出了'调用操作: XXX'这样的文字，但这只是文字描述，不是实际的工具调用！"
                        "请直接使用工具调用，不要用文字描述你要调用什么操作。")
                if fc已降级:
                    提醒 += ('\n请使用以下JSON格式调用操作：\n```json\n{"思考": "分析用户需求", '
                            '"操作": "操作名称", "参数": {"参数名": "参数值"}}\n```\n'
                            '请立即用上述格式调用工具，不要输出文字描述。')
                上下文管理器.重置本轮轨迹()
                对话历史.append({"角色": "助手", "内容": 回复内容[:500],
                                  "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
                对话历史.append({"角色": "用户", "内容": 提醒,
                                  "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
                当前观察 = 提醒
                self._推入推理流("格式提醒", {"内容": "AI输出文字而非工具调用", "重试": _fc降级重试次数})
                continue

            # 文本模式降级：解析JSON操作调用
            if fc已降级 or not fc启用:
                解析结果 = self._解析LLM输出(回复内容)
                if 解析结果["类型"] == "操作调用":
                    当前观察 = self._执行文本模式操作(
                        解析结果, 步数, 推理过程, 上下文管理器,
                        操作注册中心, 文件上下文, 意图, 对话历史)
                    # 更新连续失败
                    最后一步 = 推理过程[-1] if 推理过程 else None
                    if 最后一步 and not 最后一步.get("成功", False):
                        连续失败 += 1
                    else:
                        连续失败 = 0
                    # 批量进度检查
                    当前观察 = self._检查批量进度(
                        当前观察, 意图, 推理过程, 对话历史, 回复内容)
                    if self._检查点回调:
                        self._检查点回调(步数, 当前观察, 推理过程)
                    continue

            # === 状态: 终止（最终回复） ===
            # 批量拦截：未完成不允许结束
            拦截结果 = self._检查批量拦截(意图, 推理过程, 对话历史, 回复内容, _已确认无剩余)
            if 拦截结果:
                当前观察 = 拦截结果
                上下文管理器.重置本轮轨迹()
                continue

            最终回复 = 回复内容
            对话历史.append({"角色": "助手", "内容": 最终回复,
                              "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
            推理过程.append({"步骤": 步数, "类型": "回复", "内容": 最终回复[:200],
                             "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})
            self._推入推理流("最终回复", {"内容": 最终回复[:500], "步数": 步数})
            break

        else:
            # while-else: 达到步数上限
            最终回复 = "已达到最大推理步数，任务可能未完全完成。"
            对话历史.append({"角色": "助手", "内容": 最终回复,
                              "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")})

        # 修剪历史
        if len(对话历史) > self.最大步数:
            对话历史[:] = 对话历史[-self.最大步数:]

        # 截断推理过程给前端
        返回推理过程 = []
        for step in 推理过程:
            截断step = dict(step)
            if "结果" in 截断step and len(截断step["结果"]) > 300:
                截断step["结果"] = 截断step["结果"][:300] + "..."
            返回推理过程.append(截断step)

        return {
            "成功": True, "回复": 最终回复,
            "推理过程": 返回推理过程,
            "完整推理过程": 推理过程,
            "llm调用记录": llm调用记录,
            "步数": 步数
        }

    def _执行工具调用(self, 工具调用列表, 步数, 推理过程, 上下文管理器,
                      操作注册中心, 文件上下文, 结果, 意图, 对话历史, 模型直连器):
        """执行FC模式的工具调用"""
        思考提示 = 结果.get("回复内容", "") or ""
        原始助手消息 = 结果.get("原始响应", {}).get("choices", [{}])[0].get("message", {})

        # 保存原始assistant消息到轨迹
        if 原始助手消息 and 原始助手消息.get("tool_calls"):
            上下文管理器.添加_步骤(assistant消息=原始助手消息)

        当前观察 = ""
        for 调用 in 工具调用列表:
            英文名 = 调用.get("名称", "")
            英文参数 = 调用.get("参数", {})
            操作名, 操作参数 = 操作注册中心.解析工具调用(英文名, 英文参数)

            # 生成思考摘要
            if 思考提示:
                思考文本 = 思考提示
                思考提示 = ""
            else:
                参数摘要 = ", ".join(f"{k}={str(v)[:30]}" for k, v in 操作参数.items() if v)
                思考文本 = f"调用{操作名}({参数摘要})"

            # 框选文本拦截
            有框选 = 文件上下文 and "框选" in 文件上下文
            if 有框选 and 操作名 in ("读取文件", "读文件", "读取", "写入文件", "写文件"):
                拦截提示 = f"❌ 禁止使用「{操作名}」！当前有框选文本，必须使用「替换文本」操作！"
                推理过程.append({
                    "步骤": 步数, "类型": "操作", "操作": 操作名,
                    "参数": 操作参数, "思考": 思考文本, "结果": 拦截提示,
                    "成功": False, "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
                })
                上下文管理器.添加_步骤(tool消息={
                    "role": "tool", "tool_call_id": 调用.get("id", ""), "content": 拦截提示
                })
                当前观察 = 拦截提示
                continue

            推理过程.append({
                "步骤": 步数, "类型": "操作", "操作": 操作名,
                "参数": 操作参数, "思考": 思考文本, "fc": True,
                "时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            })
            self._推入推理流("操作调用", {"操作": 操作名, "参数": 操作参数, "思考": 思考文本[:200]})

