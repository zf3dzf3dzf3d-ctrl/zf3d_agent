"""意图解析器 — 分析用户消息，判断意图类型和参数

职责：
1. 识别闲聊/任务/批量/查看/框选编辑等意图
2. 解析批量操作数量（中文数字+阿拉伯数字）
3. 识别操作类型（生图/文件操作）

设计原则：
- 闲聊直接回复，不进ReAct循环（省token+加速）
- 批量任务返回数量参数，由推理引擎结构化控制
- 框选检测独立化，不靠循环内if判断
"""
import re


class 意图解析器类:
    """用户意图分析器"""

    # 中文数字映射
    _中文数字 = {'一': 1, '两': 2, '二': 2, '三': 3, '四': 4, '五': 5,
                 '六': 6, '七': 7, '八': 8, '九': 9, '十': 10}

    # 生图关键词
    _生图关键词 = ['生图', '出图', '生成', '画', '张图', '生视频', '出视频']

    # 文件操作关键词
    _文件操作关键词 = ['删除', '删掉', '清空', '移除', '写入', '创建', '重命名', '压缩', '解压']

    # 简单查看关键词
    _查看关键词 = ['看看', '看到', '有什么', '什么文件', '哪些文件', '列出',
                  '查看', '上到', '进去', '看一下', '看下', '是不是', '有没有']

    # 闲聊关键词（不需要工具调用，直接文字回复）
    _闲聊关键词 = ['你好', '你是谁', '你能做什么', '帮我什么', '介绍下', '谢谢',
                  '再见', '拜拜', '好的', '明白了', '知道了', '辛苦了',
                  '怎么样', '如何理解', '什么是', '为什么', '解释一下',
                  '区别', '对比', '建议', '怎么看', '你觉得', '你认为']

    def 解析(self, 用户消息: str, 文件上下文: str = "") -> dict:
        """分析用户消息，返回意图结果

        返回:
            {
                "类型": "闲聊" | "单步任务" | "批量任务" | "查看" | "框选编辑",
                "批量数": int,          # 批量任务的目标数
                "操作类型": "生图" | "文件操作" | None,
                "闲聊置信度": float,     # 0.0~1.0
            }
        """
        # 提取纯文本（去掉环境前缀）
        纯文本 = self._提取纯文本(用户消息)

        # 1. 框选编辑优先级最高
        if 文件上下文 and "框选" in 文件上下文:
            return {"类型": "框选编辑", "批量数": 0, "操作类型": None, "闲聊置信度": 0.0}

        # 2. 识别操作类型
        是生图 = any(kw in 纯文本 for kw in self._生图关键词)
        是文件操作 = any(kw in 纯文本 for kw in self._文件操作关键词)

        # 3. 解析批量数量
        批量数 = 0
        操作类型 = None
        if 是生图:
            操作类型 = "生图"
            批量数 = self._解析数量(纯文本, "张幅")
        elif 是文件操作:
            操作类型 = "文件操作"
            批量数 = self._解析数量(纯文本, "个")
            # "全部/所有"等全量关键词
            if any(kw in 纯文本 for kw in ['全部', '所有', '这几个', '这些', '都删', '全删', '每个']):
                批量数 = max(批量数, 99)

        # 4. 简单查看检测
        是查看 = any(kw in 纯文本 for kw in self._查看关键词)
        是查看 = 是查看 and not 是文件操作 and not 是生图

        # 5. 闲聊检测（非操作类才检测）
        闲聊置信度 = 0.0
        if not 是生图 and not 是文件操作 and not 是查看:
            闲聊置信度 = self._闲聊置信度(纯文本)

        # 6. 确定意图类型
        if 闲聊置信度 >= 0.6:
            意图类型 = "闲聊"
        elif 是查看:
            意图类型 = "查看"
        elif 批量数 > 1:
            意图类型 = "批量任务"
        elif 是生图 or 是文件操作:
            意图类型 = "单步任务"
        elif 闲聊置信度 > 0:
            意图类型 = "闲聊"
        else:
            意图类型 = "单步任务"  # 默认走ReAct

        return {
            "类型": 意图类型,
            "批量数": 批量数,
            "操作类型": 操作类型,
            "闲聊置信度": 闲聊置信度,
        }

    def _提取纯文本(self, 消息: str) -> str:
        """从可能包含环境前缀的消息中提取纯用户文本"""
        if "用户: " in 消息:
            return 消息.split("用户: ", 1)[-1].strip()
        if "用户指令: " in 消息:
            return 消息.split("用户指令: ", 1)[-1].strip()
        return 消息.strip()

    def _解析数量(self, 文本: str, 量词: str) -> int:
        """解析中文/阿拉伯数字+量词"""
        # 阿拉伯数字: "3张图"
        匹配 = re.search(rf'(\d+)\s*{量词}', 文本)
        if 匹配:
            return int(匹配.group(1))
        # 中文数字: "三张图"
        匹配 = re.search(rf'([一二三四五六七八九十两])\s*{量词}', 文本)
        if 匹配:
            return self._中文数字.get(匹配.group(1), 0)
        return 0

    def _闲聊置信度(self, 文本: str) -> float:
        """估算闲聊置信度（0.0~1.0）

        判断依据：
        1. 包含闲聊关键词 → 高置信度
        2. 消息短（<20字）且无操作意图 → 中等置信度
        3. 包含问号且无操作关键词 → 中等置信度
        """
        置信度 = 0.0

        # 关键词匹配
        命中数 = sum(1 for kw in self._闲聊关键词 if kw in 文本)
        if 命中数 > 0:
            置信度 = max(置信度, 0.7 + 命中数 * 0.1)

        # 短消息+无操作意图
        if len(文本) < 20 and not any(kw in 文本 for kw in self._生图关键词 + self._文件操作关键词):
            置信度 = max(置信度, 0.5)

        # 纯问句+无操作意图
        if '？' in 文本 or '?' in 文本:
            if not any(kw in 文本 for kw in self._生图关键词 + self._文件操作关键词 + self._查看关键词):
                置信度 = max(置信度, 0.6)

        return min(置信度, 1.0)
