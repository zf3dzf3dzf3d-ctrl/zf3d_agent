        闲聊模式 = fc已降级 and 工作模式 == "商量"

        # 缓存键: 基于变化频率低的因素 + 版本号 + 用户消息（操作列表按消息动态筛选）+ 紧凑模式
        缓存键 = f"v11_{fc已降级}_{工作模式}_{hash(文件上下文)}_{hash(配置.get('当前消息', '')[:200])}_{紧凑模式}"

        # 检查缓存
        if 缓存键 in self._缓存:
            缓存数据 = self._缓存[缓存键]
            部分 = [缓存数据["基础部分"]]
            # 动态部分: 记忆注入（闲聊模式跳过LLM调用，只取用户画像+近期摘要）
            if 闲聊模式:
                记忆内容 = self._获取记忆注入轻量(
                    模块注册, 配置.get("当前消息", ""), 配置.get("新对话标志", False))
            else:
                记忆内容 = self._获取记忆注入(
                    模块注册, 配置.get("当前消息", ""), 配置.get("新对话标志", False))
            if 记忆内容:
                部分.append(f"\n\n## 记忆上下文\n\n{记忆内容}")
            部分.append(缓存数据["尾部部分"])
            部分.append(f"\n\n当前时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
            if 永久记忆:
                记忆摘要 = [f"  - {m['内容']}" for m in 永久记忆[-20:]]
                部分.append(f"\n\n## 永久记忆（跨对话保留）\n" + "\n".join(记忆摘要))
            # 知识库召回（闲聊模式跳过）
            if not 闲聊模式:
                知识库 = 配置.get("知识库")
                if 知识库:
                    当前消息 = 配置.get("当前消息", "")
                    if 当前消息 and len(当前消息) > 3:
                        知识结果 = 知识库.召回文本(当前消息)
                        if 知识结果:
                            部分.append(f"\n\n## 知识库参考\n\n以下是与当前对话相关的知识库内容：\n\n{知识结果}")
