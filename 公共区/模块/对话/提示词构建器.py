        fc配置 = 模型直连器.配置.get("function_calling", {}) if 模型直连器 else {}
        fc启用 = fc配置.get("启用", False) and not fc已降级
        基础部分列表.append(self._构建_react指令(fc启用))

        # 6. 可用操作列表（闲聊模式跳过，省~15K字符）
        if 操作注册中心 and not 闲聊模式:
            if 紧凑模式:
                操作说明 = 操作注册中心.获取紧凑操作说明(已选操作集)
