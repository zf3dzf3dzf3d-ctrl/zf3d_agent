"""任务模块 - 任务拆解+执行调度+进度追踪

工作流：
1. 用户提出任务 → LLM拆解为子步骤
2. 逐步执行子步骤（调用操作注册中心）
3. 追踪进度，失败可重试
4. 完成后汇总结果
"""
import json
from pathlib import Path
from datetime import datetime

from 配置加载器 import 全局事件中心


class 任务模块:
    def __init__(self):
        self.配置 = {}
        self.模型直连器 = None
        self.操作注册中心 = None
        self.模块注册 = None
        self.任务队列 = []
        self.任务计数 = 0
        self.当前任务 = None

    def 初始化(self, 配置: dict):
        """初始化任务模块"""
        self.配置 = 配置
        self.模型直连器 = 配置.get("模型直连器")
        self.操作注册中心 = 配置.get("操作注册中心")
        self.模块注册 = 配置.get("模块注册", {})

    def 运行(self, 输入数据: dict) -> dict:
        """执行任务操作"""
        操作 = 输入数据.get("操作", "")

        if 操作 == "创建任务":
            return self._创建任务(输入数据.get("任务名", ""), 输入数据.get("描述", ""))
        elif 操作 == "拆解任务":
            return self._拆解任务(输入数据.get("任务编号", 0))
        elif 操作 == "执行任务":
            return self._执行任务(输入数据.get("任务编号", 0))
        elif 操作 == "获取进度":
            return self._获取进度(输入数据.get("任务编号", 0))
        elif 操作 == "列出任务":
            return self._列出任务()
        elif 操作 == "取消任务":
            return self._取消任务(输入数据.get("任务编号", 0))
        else:
            return {"成功": False, "错误": f"未知操作: {操作}"}

    def _创建任务(self, 任务名: str, 描述: str = "") -> dict:
        """创建新任务"""
        if not 任务名:
            return {"成功": False, "错误": "任务名为空"}

        self.任务计数 += 1
        任务 = {
            "编号": self.任务计数,
            "名称": 任务名,
            "描述": 描述 or 任务名,
            "状态": "待拆解",
            "步骤": [],
            "当前步骤": 0,
            "结果": [],
            "创建时间": datetime.now().isoformat(),
            "完成时间": None
        }
        self.任务队列.append(任务)
        return {"成功": True, "任务编号": self.任务计数, "状态": "已创建，待拆解"}

    def _拆解任务(self, 任务编号: int) -> dict:
        """用LLM拆解任务为步骤"""
        任务 = self._查找任务(任务编号)
        if not 任务:
            return {"成功": False, "错误": f"任务 {任务编号} 不存在"}

        if not self.模型直连器:
            return {"成功": False, "错误": "模型直连器未初始化"}

        # 构建拆解请求
        操作列表 = ""
        if self.操作注册中心:
            操作列表 = self.操作注册中心.获取操作说明()

        拆解提示词 = f"""请将以下任务拆解为可执行的步骤。

任务: {任务['名称']}
描述: {任务['描述']}

可用操作:
{操作列表}

请返回JSON格式的步骤列表:
[
  {{"步骤": 1, "说明": "做什么", "操作": "操作名称", "参数": {{"参数名": "参数值"}}}},
  {{"步骤": 2, "说明": "...", "操作": "...", "参数": {{...}}}}
]

只返回JSON数组，不要其他内容。"""

        try:
            结果 = self.模型直连器.发送消息(
                [{"role": "user", "content": 拆解提示词}],
                "你是任务拆解专家，只返回JSON数组。"
            )
            if not 结果["成功"]:
                return {"成功": False, "错误": f"拆解失败: {结果.get('错误', '')}"}

            # 解析步骤
            import re
            回复 = 结果["回复内容"]
            json匹配 = re.search(r'\[[\s\S]*\]', 回复)
            if not json匹配:
                return {"成功": False, "错误": "无法解析拆解结果"}

            步骤列表 = json.loads(json匹配.group())
            任务["步骤"] = 步骤列表
            任务["状态"] = "待执行"
            return {"成功": True, "任务编号": 任务编号, "步骤数": len(步骤列表), "步骤": 步骤列表}

        except Exception as e:
            return {"成功": False, "错误": f"拆解异常: {e}"}

    def _执行任务(self, 任务编号: int) -> dict:
        """执行任务的下一步"""
        任务 = self._查找任务(任务编号)
        if not 任务:
            return {"成功": False, "错误": f"任务 {任务编号} 不存在"}

        if 任务["状态"] not in ("待执行", "执行中"):
            return {"成功": False, "错误": f"任务状态为 {任务['状态']}，无法执行"}

        if not 任务["步骤"]:
            return {"成功": False, "错误": "任务未拆解，请先拆解"}

        # 执行下一步
        当前索引 = 任务["当前步骤"]
        if 当前索引 >= len(任务["步骤"]):
            任务["状态"] = "已完成"
            任务["完成时间"] = datetime.now().isoformat()
            return {"成功": True, "消息": "任务已完成", "结果列表": 任务["结果"]}

        步骤 = 任务["步骤"][当前索引]
        任务["状态"] = "执行中"

        # 执行操作
        操作名 = 步骤.get("操作", "")
        操作参数 = 步骤.get("参数", {})

        if self.操作注册中心 and 操作名:
            执行结果 = self.操作注册中心.执行(操作名, 操作参数)
            任务["结果"].append({
                "步骤": 当前索引 + 1,
                "操作": 操作名,
                "参数": 操作参数,
                "结果": 执行结果
            })

            任务["当前步骤"] = 当前索引 + 1

            if not 执行结果.get("成功"):
                任务["状态"] = "执行失败"
                return {"成功": False, "错误": f"步骤{当前索引+1}执行失败: {执行结果.get('错误', '')}"}

            # 检查是否全部完成
            if 任务["当前步骤"] >= len(任务["步骤"]):
                任务["状态"] = "已完成"
                任务["完成时间"] = datetime.now().isoformat()
                return {"成功": True, "消息": "任务全部完成", "结果列表": 任务["结果"]}

            return {"成功": True, "消息": f"步骤{当前索引+1}完成", "下一步": 当前索引 + 2}
        else:
            任务["结果"].append({
                "步骤": 当前索引 + 1,
                "操作": 操作名 or "(无操作)",
                "结果": {"成功": False, "错误": "无操作名称"}
            })
            任务["当前步骤"] = 当前索引 + 1
            return {"成功": False, "错误": f"步骤{当前索引+1}缺少操作名称"}

    def _获取进度(self, 任务编号: int) -> dict:
        """获取任务进度"""
        任务 = self._查找任务(任务编号)
        if not 任务:
            # 返回所有任务进度
            return {"成功": True, "任务列表": [{
                "编号": t["编号"], "名称": t["名称"],
                "状态": t["状态"], "进度": f"{t['当前步骤']}/{len(t['步骤'])}"
            } for t in self.任务队列]}

        总步数 = len(任务["步骤"])
        当前 = 任务["当前步骤"]
        return {
            "成功": True,
            "编号": 任务["编号"],
            "名称": 任务["名称"],
            "状态": 任务["状态"],
            "进度": f"{当前}/{总步数}",
            "百分比": int(当前 / 总步数 * 100) if 总步数 else 0
        }

    def _列出任务(self) -> dict:
        """列出所有任务"""
        return {"成功": True, "任务列表": [{
            "编号": t["编号"], "名称": t["名称"], "状态": t["状态"],
            "步骤数": len(t["步骤"]), "当前步骤": t["当前步骤"]
        } for t in self.任务队列]}

    def _取消任务(self, 任务编号: int) -> dict:
        """取消任务"""
        任务 = self._查找任务(任务编号)
        if not 任务:
            return {"成功": False, "错误": f"任务 {任务编号} 不存在"}
        任务["状态"] = "已取消"
        return {"成功": True, "消息": f"任务 {任务编号} 已取消"}

    def _查找任务(self, 编号: int) -> dict:
        """查找指定编号的任务"""
        for 任务 in self.任务队列:
            if 任务["编号"] == 编号:
                return 任务
        return None

    def 停止(self):
        """停止任务模块"""
        self.任务队列.clear()

    def 获取任务列表(self) -> list:
        """获取所有任务"""
        return self.任务队列

    def 获取任务(self, 编号: int) -> dict:
        """获取指定任务"""
        return self._查找任务(编号) or {}
