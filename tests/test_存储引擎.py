class Test存储引擎_剧本:

    def test_保存并加载剧本(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        剧本内容 = json.dumps({"名称": "测试剧本", "步骤": [{"序号": 1, "操作": "获取时间"}]}, ensure_ascii=False)
        引擎.保存剧本("测试剧本", 剧本内容)

        加载内容 = 引擎.加载剧本("测试剧本")
        assert 加载内容 is not None
        加载剧本 = json.loads(加载内容)
        assert 加载剧本["名称"] == "测试剧本"
        引擎.关闭()

    def test_列出剧本(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        引擎.保存剧本("剧本A", "{}")
        引擎.保存剧本("剧本B", "{}")

        列表 = 引擎.列出剧本()
        assert len(列表) == 2
        引擎.关闭()

    def test_删除剧本(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        引擎.保存剧本("待删剧本", "{}")
        引擎.删除剧本("待删剧本")

        assert 引擎.加载剧本("待删剧本") is None
        引擎.关闭()


class Test存储引擎_迁移:

    def test_迁移对话记录(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        # 创建模拟JSON对话文件
        对话目录 = 临时目录 / "对话记录"
        对话目录.mkdir()
        对话数据 = {
            "历史": [
                {"角色": "user", "内容": "迁移测试", "时间": "2026-06-24 10:00:00"},
                {"角色": "assistant", "内容": "迁移成功", "时间": "2026-06-24 10:00:01"}
            ]
        }
        with open(对话目录 / "test_chat.json", "w", encoding="utf-8") as f:
            json.dump(对话数据, f, ensure_ascii=False)

        结果 = 引擎.迁移对话记录(str(对话目录))
        assert 结果["迁移数"] == 1
        assert 结果["消息数"] == 2

        消息 = 引擎.查询对话消息("test_chat")
        assert len(消息) == 2
        引擎.关闭()
