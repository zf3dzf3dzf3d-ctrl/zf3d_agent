class Test存储引擎_对话记录:

    def test_插入并查询(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        引擎.插入对话消息("对话1", "user", "你好", "2026-06-24 10:00:00")
        引擎.插入对话消息("对话1", "assistant", "你好！有什么可以帮你的？", "2026-06-24 10:00:01")

        消息列表 = 引擎.查询对话消息("对话1")
        assert len(消息列表) == 2
        assert 消息列表[0]["角色"] == "user"
        assert 消息列表[0]["内容"] == "你好"
        assert 消息列表[1]["角色"] == "assistant"
        引擎.关闭()

    def test_删除对话(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        引擎.插入对话消息("对话A", "user", "内容A")
        引擎.删除对话("对话A")
        消息列表 = 引擎.查询对话消息("对话A")
        assert len(消息列表) == 0
        引擎.关闭()

    def test_批量插入(self, 临时目录):
        引擎 = 存储引擎类(str(临时目录 / "test.db"))
        消息列表 = [
            {"对话ID": "批量对话", "角色": "user", "内容": "消息1", "时间": "2026-06-24 10:00:00"},
            {"对话ID": "批量对话", "角色": "assistant", "内容": "回复1", "时间": "2026-06-24 10:00:01"},
            {"对话ID": "批量对话", "角色": "user", "内容": "消息2", "时间": "2026-06-24 10:00:02"},
        ]
        引擎.批量插入对话消息(消息列表)
        结果 = 引擎.查询对话消息("批量对话")
        assert len(结果) == 3
        引擎.关闭()


