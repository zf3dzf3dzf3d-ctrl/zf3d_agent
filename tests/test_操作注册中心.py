class Test操作注册中心_执行:

    def test_执行成功(self):
        中心 = 操作注册中心类()

        class 测试操作(操作基类):
            名称 = "执行测试"
            描述 = "测试"
            参数结构 = {"输入": {"类型": "字符串", "必填": True, "说明": "输入"}}

            def 执行(self, 参数):
                return 操作结果.成功(f"收到: {参数.get('输入', '')}")

        中心.注册(测试操作())
        结果 = 中心.执行("执行测试", {"输入": "hello"})
        assert 结果["成功"]
        assert "hello" in 结果["数据"]

