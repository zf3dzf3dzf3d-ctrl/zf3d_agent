class Test剧本管理器_录制:

    def test_开始和停止录制(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        开始结果 = 管理器.开始录制("测试剧本")
        assert 开始结果["成功"]
        assert 管理器.正在录制

        # 记录几个操作
        管理器.记录操作("获取时间", {}, {"成功": True, "数据": "2026-06-24"})
        管理器.记录操作("数学计算", {"表达式": "1+1"}, {"成功": True, "数据": "2"})

        停止结果 = 管理器.停止录制()
        assert 停止结果["成功"]
        assert 停止结果["步数"] == 2
        assert not 管理器.正在录制

    def test_重复开始录制被拒(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        管理器.开始录制("剧本1")
        结果 = 管理器.开始录制("剧本2")
        assert not 结果["成功"]
        管理器.停止录制()

    def test_无录制时停止失败(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        结果 = 管理器.停止录制()
        assert not 结果["成功"]


class Test剧本管理器_持久化:

    def test_保存并加载剧本(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        剧本 = {
            "名称": "持久化测试",
            "创建时间": "2026-06-24 10:00:00",
            "步骤": [{"序号": 1, "操作": "获取时间", "参数": {}, "等待完成": True}]
        }
        保存结果 = 管理器.保存剧本(剧本)
        assert 保存结果["成功"]

        加载结果 = 管理器.加载剧本("持久化测试")
        assert 加载结果["成功"]
        assert 加载结果["剧本"]["名称"] == "持久化测试"

    def test_列出剧本(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        管理器.保存剧本({"名称": "剧本A", "创建时间": "2026-01-01 00:00:00", "步骤": []})
        管理器.保存剧本({"名称": "剧本B", "创建时间": "2026-01-02 00:00:00", "步骤": []})

        列表 = 管理器.列出剧本()
        assert len(列表) >= 2

    def test_删除剧本(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        管理器.保存剧本({"名称": "待删", "创建时间": "2026-01-01 00:00:00", "步骤": []})
        结果 = 管理器.删除剧本("待删")
        assert 结果["成功"]

        加载结果 = 管理器.加载剧本("待删")
        assert not 加载结果["成功"]


class Test剧本管理器_变量替换:

    def test_简单变量替换(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        上下文 = {"步骤1": {"结果": "替换值"}}
        参数 = {"内容": "${步骤1.结果}"}
        结果 = 管理器._替换变量(参数, 上下文)
        assert 结果["内容"] == "替换值"

    def test_嵌套变量替换(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        上下文 = {"步骤1": {"结果": "值A"}, "步骤2": {"结果": "值B"}}
        参数 = {"a": "${步骤1.结果}", "b": "${步骤2.结果}"}
        结果 = 管理器._替换变量(参数, 上下文)
        assert 结果["a"] == "值A"
        assert 结果["b"] == "值B"

    def test_列表中变量替换(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        上下文 = {"步骤1": {"结果": "列表值"}}
        参数 = ["${步骤1.结果}", "固定值"]
        结果 = 管理器._替换变量(参数, 上下文)
        assert 结果[0] == "列表值"
        assert 结果[1] == "固定值"

    def test_无变量不替换(self, 临时目录):
        管理器 = 剧本管理器(操作注册中心=None, 项目根目录=str(临时目录))
        上下文 = {}
        参数 = {"内容": "固定内容"}
        结果 = 管理器._替换变量(参数, 上下文)
        assert 结果["内容"] == "固定内容"
