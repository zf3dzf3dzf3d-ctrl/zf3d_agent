class Test文件管理器_权限校验:

    def test_AI调用未授权路径被拒(self, 临时目录, 基础权限配置):
        管理器 = 文件管理器类(基础权限配置, 临时目录)
        # 写到完全不在授权目录下的路径（用绝对路径指向临时目录外）
        外部路径 = str(临时目录.parent / "未授权目录" / "文件.txt")
        结果 = 管理器.写入文件(外部路径, "内容", AI调用=True)
        assert not 结果["成功"]

    def test_禁止后缀AI调用被拒(self, 临时目录, 基础权限配置):
        管理器 = 文件管理器类(基础权限配置, 临时目录)
        # 禁止后缀检查仅对AI调用生效
        结果 = 管理器.写入文件("./恶意.exe", "内容", AI调用=True)
        assert not 结果["成功"]

    def test_列目录(self, 临时目录, 基础权限配置):
        管理器 = 文件管理器类(基础权限配置, 临时目录)
        管理器.写入文件("./文件A.txt", "a")
        管理器.写入文件("./文件B.py", "b")
        结果 = 管理器.列目录("./")
        assert 结果["成功"]
        名称列表 = [项["名称"] for 项 in 结果["内容"]]
        assert "文件A.txt" in 名称列表
        assert "文件B.py" in 名称列表


