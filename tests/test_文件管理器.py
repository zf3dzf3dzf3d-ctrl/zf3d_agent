class Test文件管理器_批量替换:

    def test_批量替换成功(self, 临时目录, 基础权限配置):
        管理器 = 文件管理器类(基础权限配置, 临时目录)
        管理器.写入文件("./批量.txt", "苹果 香蕉 橘子")
        结果 = 管理器.批量替换("./批量.txt", [
            {"旧文本": "苹果", "新文本": "apple"},
            {"旧文本": "香蕉", "新文本": "banana"},
        ])
        assert 结果["成功"]
        读结果 = 管理器.读取文件("./批量.txt")
        assert "apple" in 读结果["内容"]
        assert "banana" in 读结果["内容"]

    def test_批量替换全部无匹配(self, 临时目录, 基础权限配置):
        管理器 = 文件管理器类(基础权限配置, 临时目录)
        管理器.写入文件("./空批量.txt", "内容")
        结果 = 管理器.批量替换("./空批量.txt", [
            {"旧文本": "不存在1", "新文本": "a"},
            {"旧文本": "不存在2", "新文本": "b"},
        ])
        assert not 结果["成功"]
